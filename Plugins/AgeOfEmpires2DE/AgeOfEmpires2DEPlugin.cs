    // ── News feed ─────────────────────────────────────────────────────────────
    // The Ageipelago GitHub releases are the most honest versioned "news" for this
    // integration. Never throws — empty on failure.

    public async Task<NewsItem[]> GetNewsAsync(CancellationToken ct = default)
    {
        try
        {
            string json = await _http.GetStringAsync(ApworldReleasesApiUrl, ct);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.ValueKind != JsonValueKind.Array)
                return Array.Empty<NewsItem>();

            var items = new List<NewsItem>();
            foreach (var el in doc.RootElement.EnumerateArray())
            {
                DateTimeOffset date = DateTimeOffset.MinValue;
                if (el.TryGetProperty("published_at", out var d) &&
                    d.ValueKind == JsonValueKind.String)
                    DateTimeOffset.TryParse(d.GetString(), out date);

                items.Add(new NewsItem(
                    Title:   el.TryGetProperty("name",     out var n) ? n.GetString() ?? "" : "",
                    Body:    el.TryGetProperty("body",     out var b) ? b.GetString() ?? "" : "",
                    Version: el.TryGetProperty("tag_name", out var t) ? t.GetString() ?? "" : "",
                    Date:    date,
                    Url:     el.TryGetProperty("html_url", out var u) ? u.GetString() : null
                ));
                if (items.Count >= 10) break;
            }
            return items.ToArray();
        }
        catch { return Array.Empty<NewsItem>(); }
    }

    // ── Private helpers — AoE2 DE install detection ───────────────────────────

    private string? ResolveSteamDir()
    {
        if (!string.IsNullOrWhiteSpace(_overrideRootDir) &&
            LooksLikeAoE2Root(_overrideRootDir))
            return _overrideRootDir;
        try { return DetectSteamAoE2Dir(); }
        catch { return null; }
    }

    private static bool LooksLikeAoE2Root(string dir)
    {
        try
        {
            if (string.IsNullOrWhiteSpace(dir) || !Directory.Exists(dir)) return false;
            foreach (string exe in GameExeNames)
                if (File.Exists(Path.Combine(dir, exe))) return true;
            return false;
        }
        catch { return false; }
    }

    private static string? DetectSteamAoE2Dir()
    {
        foreach (string steamRoot in SteamRoots())
        {
            if (string.IsNullOrWhiteSpace(steamRoot)) continue;
            foreach (string lib in SteamLibraryRoots(steamRoot))
            {
                try
                {
                    string steamapps = Path.Combine(lib, "steamapps");
                    string manifest  = Path.Combine(steamapps, "appmanifest_" + SteamAppId + ".acf");
                    if (!File.Exists(manifest)) continue;

                    string common     = Path.Combine(steamapps, "common");
                    string? installDir = ReadAcfInstallDir(manifest);
                    if (installDir != null)
                    {
                        string candidate = Path.Combine(common, installDir);
                        if (LooksLikeAoE2Root(candidate)) return candidate;
                    }
                    string conventional = Path.Combine(common, SteamCommonFolderName);
                    if (LooksLikeAoE2Root(conventional)) return conventional;
                }
                catch { /* try next library */ }
            }
        }
        return null;
    }

    // ── Private helpers — AoE2 DE user folder detection ──────────────────────

    /// The AoE2 DE user folder: override wins, else the first
    /// C:\Users\<USER>\Games\Age of Empires 2 DE\<numeric-id>\ folder found.
    private string? ResolveUserDir()
    {
        if (!string.IsNullOrWhiteSpace(_overrideUserDir) &&
            Directory.Exists(_overrideUserDir))
            return _overrideUserDir;
        try { return DetectAoE2UserDir(); }
        catch { return null; }
    }

    private static string? DetectAoE2UserDir()
    {
        // AoE2 DE stores user data under %USERPROFILE%\Games\Age of Empires 2 DE\<numeric-id>\
        string? userProfile = null;
        try { userProfile = Environment.GetFolderPath(Environment.SpecialFolder.UserProfile); }
        catch { }
        if (string.IsNullOrWhiteSpace(userProfile)) return null;

        string root = Path.Combine(userProfile, "Games", GamesRootFolderName);
        if (!Directory.Exists(root)) return null;

        // There is normally exactly one numeric subfolder (the Xbox/Steam user ID).
        foreach (string sub in Directory.EnumerateDirectories(root))
        {
            string name = Path.GetFileName(sub);
            if (IsNumericFolderName(name)) return sub;
        }
        return null;
    }

    private static bool IsNumericFolderName(string name)
    {
        if (string.IsNullOrWhiteSpace(name)) return false;
        foreach (char c in name)
            if (c < '0' || c > '9') return false;
        return name.Length > 0;
    }

    // ── Private helpers — Archipelago install / client (READ-ONLY) ───────────

    private string? ResolveArchipelagoRoot()
    {
        if (!string.IsNullOrWhiteSpace(_overrideApDir) &&
            Directory.Exists(_overrideApDir))
            return _overrideApDir;
        try
        {
            if (Directory.Exists(DefaultArchipelagoRoot))
                return DefaultArchipelagoRoot;
            foreach (Environment.SpecialFolder sf in new[]
            {
                Environment.SpecialFolder.LocalApplicationData,
                Environment.SpecialFolder.ProgramFiles,
                Environment.SpecialFolder.ProgramFilesX86,
            })
            {
                string? baseDir = null;
                try { baseDir = Environment.GetFolderPath(sf); } catch { }
                if (!string.IsNullOrWhiteSpace(baseDir))
                {
                    string cand = Path.Combine(baseDir, "Archipelago");
                    if (Directory.Exists(cand)) return cand;
                }
            }
        }
        catch { }
        return null;
    }

    private string? ResolveBundledClient()
    {
        try
        {
            string? root = ResolveArchipelagoRoot();
            if (root == null || !Directory.Exists(root)) return null;

            // Prefer a dedicated Age2 client exe if one was bundled.
            string? age2Client = FindExe(root,
                name => (name.Contains("age") && name.Contains("2") &&
                         name.Contains("client")) ||
                        (name.Contains("aoe") && name.Contains("client")));
            if (age2Client != null) return age2Client;

            // Fall back to the generic AP launcher (we pass it the component name).
            string launcher = Path.Combine(root, ArchipelagoLauncherExe);
            if (File.Exists(launcher)) return launcher;

            return FindExe(root,
                name => name.Contains("archipelagolauncher") ||
                       (name.Contains("archipelago") && name.Contains("launcher")));
        }
        catch { return null; }
    }

    private static bool IsGenericApLauncher(string exePath)
    {
        string name = Path.GetFileNameWithoutExtension(exePath).ToLowerInvariant();
        return name.Contains("launcher") && !name.Contains("age");
    }

    private static string? FindExe(string dir, Func<string, bool> predicate)
    {
        try
        {
            if (!Directory.Exists(dir)) return null;
            foreach (string exe in Directory.EnumerateFiles(dir, "*.exe",
                                                            SearchOption.TopDirectoryOnly))
            {
                string name = Path.GetFileNameWithoutExtension(exe).ToLowerInvariant();
                if (predicate(name)) return exe;
            }
        }
        catch { }
        return null;
    }

    // ── Private helpers — launch ──────────────────────────────────────────────

    private void StartBundledClient(string clientExe)
    {
        var psi = new ProcessStartInfo
        {
            FileName         = clientExe,
            WorkingDirectory = Path.GetDirectoryName(clientExe) ?? "",
            UseShellExecute  = false,
        };
        // If it is the generic AP launcher, pass the component name to dispatch.
        if (IsGenericApLauncher(clientExe))
            psi.Arguments = Quote(Age2ClientComponentName);

        var proc = Process.Start(psi);
        if (proc == null) return;

        _clientProcess = proc;
        IsRunning      = true;
        try
        {
            proc.EnableRaisingEvents = true;
            proc.Exited += (_, _) =>
            {
                if (_gameProcess == null || _gameProcess.HasExited)
                    IsRunning = false;
            };
        }
        catch { /* non-fatal */ }
    }

    private void StartAoE2DE()
    {
        string? root = ResolveSteamDir();
        string? exe  = null;
        if (root != null)
        {
            foreach (string name in GameExeNames)
            {
                string cand = Path.Combine(root, name);
                if (File.Exists(cand)) { exe = cand; break; }
            }
        }

        if (exe != null)
        {
            var proc = Process.Start(new ProcessStartInfo
            {
                FileName         = exe,
                WorkingDirectory = root!,
                UseShellExecute  = true,
            });
            if (proc != null)
            {
                _gameProcess = proc;
                IsRunning    = true;
                try
                {
                    proc.EnableRaisingEvents = true;
                    proc.Exited += (_, _) =>
                    {
                        IsRunning = false;
                        GameExited?.Invoke(proc.ExitCode);
                    };
                }
                catch { /* non-fatal */ }
            }
            return;
        }

        // Fall back to Steam URL (preferred for AoE2 anyway — lets Steam verify DLC).
        if (SteamIsInstalled())
        {
            try
            {
                Process.Start(new ProcessStartInfo(SteamRunUrl) { UseShellExecute = true });
                IsRunning = true;
                return;
            }
            catch { /* fall through */ }
        }

        throw new FileNotFoundException(
            "Could not find AoE2DE_s.exe. Open this game's Settings and pick your " +
            "AoE2 DE install folder, or install the game via Steam (appid " +
            SteamAppId + ").",
            GameExeNames[0]);
    }

    private static bool SteamIsInstalled()
    {
        foreach (string r in SteamRoots())
        {
            try { if (!string.IsNullOrWhiteSpace(r) && Directory.Exists(r)) return true; }
            catch { }
        }
        return false;
    }

    private static string Quote(string value)
    {
        if (string.IsNullOrEmpty(value)) return "\"\"";
        bool needs = value.IndexOfAny(new[] { ' ', '\t', '"' }) >= 0;
        if (!needs) return value;
        return "\"" + value.Replace("\"", "\\\"") + "\"";
    }

    // ── Private helpers — Steam detection (standard pipeline) ─────────────────

    private static IEnumerable<string> SteamRoots()
    {
        string? hkcu = ReadRegistryString(Registry.CurrentUser,
            @"Software\Valve\Steam", "SteamPath");
        if (!string.IsNullOrWhiteSpace(hkcu)) yield return NormalizeSteamPath(hkcu);

        string? hklm = ReadRegistryString(Registry.LocalMachine,
            @"SOFTWARE\WOW6432Node\Valve\Steam", "InstallPath");
        if (!string.IsNullOrWhiteSpace(hklm)) yield return NormalizeSteamPath(hklm);

        string? hklm64 = ReadRegistryString(Registry.LocalMachine,
            @"SOFTWARE\Valve\Steam", "InstallPath");
        if (!string.IsNullOrWhiteSpace(hklm64)) yield return NormalizeSteamPath(hklm64);

        string? progX86 = Environment.GetFolderPath(
            Environment.SpecialFolder.ProgramFilesX86);
        if (!string.IsNullOrWhiteSpace(progX86))
            yield return Path.Combine(progX86, "Steam");
    }

    private static string NormalizeSteamPath(string p)
        => p.Replace('/', '\\').TrimEnd('\\');

    private static IEnumerable<string> SteamLibraryRoots(string steamRoot)
    {
        var seen = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        if (seen.Add(steamRoot)) yield return steamRoot;

        string vdf = Path.Combine(steamRoot, "steamapps", "libraryfolders.vdf");
        if (!File.Exists(vdf)) yield break;

        string text;
        try { text = File.ReadAllText(vdf); }
        catch { yield break; }

        foreach (string path in ExtractVdfPaths(text))
        {
            string norm = path.Replace('/', '\\').TrimEnd('\\');
            if (norm.Length > 0 && seen.Add(norm))
                yield return norm;
        }
    }

    private static IEnumerable<string> ExtractVdfPaths(string text)
    {
        const string key = "\"path\"";
        int i = 0;
        while ((i = text.IndexOf(key, i, StringComparison.OrdinalIgnoreCase)) >= 0)
        {
            i += key.Length;
            int open = text.IndexOf('"', i);
            if (open < 0) yield break;
            int close = text.IndexOf('"', open + 1);
            if (close < 0) yield break;
            string raw = text.Substring(open + 1, close - open - 1);
            yield return raw.Replace("\\\\", "\\");
            i = close + 1;
        }
    }

    private static string? ReadAcfInstallDir(string acfPath)
    {
        try
        {
            string text = File.ReadAllText(acfPath);
            const string key = "\"installdir\"";
            int i = text.IndexOf(key, StringComparison.OrdinalIgnoreCase);
            if (i < 0) return null;
            i += key.Length;
            int open = text.IndexOf('"', i);
            if (open < 0) return null;
            int close = text.IndexOf('"', open + 1);
            if (close < 0) return null;
            return text.Substring(open + 1, close - open - 1).Replace("\\\\", "\\");
        }
        catch { return null; }
    }

    private static string? ReadRegistryString(
        RegistryKey hive, string subKey, string valueName)
    {
        try
        {
            using RegistryKey? k = hive.OpenSubKey(subKey);
            return k?.GetValue(valueName) as string;
        }
        catch { return null; }
    }

