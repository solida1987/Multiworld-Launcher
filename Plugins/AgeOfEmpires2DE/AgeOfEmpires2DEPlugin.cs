    private Age2DESettings LoadSettings()
    {
        try
        {
            if (File.Exists(SettingsSidecarPath))
            {
                string txt = File.ReadAllText(SettingsSidecarPath);
                if (!string.IsNullOrWhiteSpace(txt))
                    return JsonSerializer.Deserialize<Age2DESettings>(txt) ?? new();
            }
        }
        catch { /* corrupt → defaults */ }
        return new();
    }

    private void SaveSettings(Age2DESettings s)
    {
        try
        {
            Directory.CreateDirectory(SettingsSidecarDir);
            File.WriteAllText(SettingsSidecarPath,
                JsonSerializer.Serialize(s,
                    new JsonSerializerOptions { WriteIndented = true }),
                new UTF8Encoding(false));
        }
        catch { /* non-fatal */ }
    }

    private void SaveRootDirOverride(string dir)
    {
        var s = LoadSettings(); s.RootDirOverride = dir; SaveSettings(s);
    }

    private void SaveUserDirOverride(string dir)
    {
        var s = LoadSettings(); s.UserDirOverride = dir; SaveSettings(s);
    }

    private void SaveApInstallOverride(string dir)
    {
        var s = LoadSettings(); s.ApInstallOverride = dir; SaveSettings(s);
    }
