// ═══════════════════════════════════════════════════════════════════════════════
// AgeOfEmpires2DEPlugin — detect / guide / launch for "Age of Empires II:
// Definitive Edition" (Xbox Game Studios / Microsoft, 2019) played through the
// COMMUNITY Archipelago integration by dmwever and Stefrex1. This is a
// "ConnectsItself" integration: the bundled "Age of Empires II: DE Client"
// (a Python component inside the player's Archipelago release) owns the AP slot
// and relays to the running game — the launcher must NOT hold its own ApClient
// on the slot.
//
// ── HONEST REALITY CHECK (2026-06-15) — verified against dmwever/Archipelago ──
// The integration is a COMMUNITY CUSTOM apworld (NOT in AP-main), distributed via:
//   https://github.com/dmwever/Ageipelago/releases
//
// The pieces are:
//
//   * THE BASE GAME is the user's own legally-owned Age of Empires II: Definitive
//     Edition (Steam appid 813780, Xbox Game Studios). Paid software — the launcher
//     must not, and does not, ship or recreate it.
//
//   * THE APWORLD FILE is the .apworld from Ageipelago releases. The player
//     installs it into their Archipelago custom_worlds folder (or via the AP
//     Launcher) before generating a room. This is NOT bundled by this launcher.
//
//   * THE "Age of Empires II: DE Client" is a PYTHON client SHIPPED INSIDE the
//     player's Archipelago release. It is registered in worlds/age2de/__init__.py
//     as  Component("Age of Empires II: DE Client", func=run_client,
//     component_type=Type.CLIENT)  and launched as component "Age2Client".
//     VERIFIED against dmwever/Archipelago worlds/age2de/__init__.py.
//     The client holds the AP server connection and communicates with the running
//     game via FILE IPC: it reads/writes the user's AoE2 DE user folder
//     (C:\Users\<USER>\Games\Age of Empires 2 DE\<ID>\) — saved game files and
//     a dedicated AP data folder that the game's mod reads. VERIFIED from the
//     client source (ApClient.py/GameClient.py).
//
//   * THE MOD is installed via the AoE2 DE Steam Workshop or bundled with the
//     apworld. It runs inside the game engine and communicates with the client
//     via the user folder file IPC. The player must subscribe/install this mod
//     and enable it in-game. The per-room .aoe2scenario files generated by the
//     apworld are placed into the user's scenario folder.
//
//   * CONNECTION: the player enters server / slot / password INTO THE "Age of
//     Empires II: DE Client" — there is no connection arg on the game and no
//     config file this launcher can pre-write. Hence ConnectsItself = true: the
//     launcher must NOT also sit on the slot or the two would kick each other.
//
// ── WHAT THIS PLUGIN HONESTLY DOES ───────────────────────────────────────────
//   1. DETECT the Steam AoE2 DE install (appid 813780) via the standard
//      registry → libraryfolders.vdf → appmanifest_813780.acf → common pipeline.
//      A manual root-dir OVERRIDE (folder picker) is supported and persisted in
//      this plugin's OWN sidecar (Games/ROMs/age2de/age2de_launcher.json).
//   2. DETECT the AoE2 DE user folder under C:\Users\<USER>\Games\Age of
//      Empires 2 DE\  (the <ID> subfolder), which the AP client must be
//      configured to point at. A manual override is also supported.
//   3. LOCATE the bundled AP client in the user's Archipelago install (READ-ONLY:
//      C:\ProgramData\Archipelago is NEVER written). Prefer any exe matching
//      "*age*2*client*.exe" or "*aoe2*client*.exe"; fall back to
//      ArchipelagoLauncher.exe (which dispatches by component name).
//   4. LAUNCH (best effort): start the "Age of Empires II: DE Client" (it owns
//      the AP slot + relays to the game), then open AoE2 DE (via the Steam URL
//      steam://rungameid/813780 or the detected exe).
//   5. GUIDE the per-room .aoe2scenario setup step and mod-enable step honestly.
//   6. NEVER claim an install exists when it does not; never modify the Steam copy.
//
// ── GAME STRING (VERIFIED against dmwever/Archipelago worlds/age2de/__init__.py) ─
//   AGE2_DE = "Age Of Empires II: Definitive Edition"
//   class Age2World: game = AGE2_DE
//   Component("Age of Empires II: DE Client", func=run_client, Type.CLIENT)
// ═══════════════════════════════════════════════════════════════════════════════

