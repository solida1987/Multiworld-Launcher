    // ── Existing-install validation (override picker) ─────────────────────────

    public string? ValidateExistingInstall(string folder)
    {
        if (string.IsNullOrWhiteSpace(folder) || !Directory.Exists(folder))
            return "That folder does not exist. Pick your AoE2 DE install folder " +
                   "(the one containing AoE2DE_s.exe).";
        if (LooksLikeAoE2Root(folder)) return null;

        try
        {
            string nested = Path.Combine(folder, SteamCommonFolderName);
            if (LooksLikeAoE2Root(nested)) return null;
        }
        catch { /* ignore */ }

        return "That does not look like an Age of Empires II: DE installation. Pick " +
               "the folder that contains AoE2DE_s.exe (usually " +
               @"...\steamapps\common\AoE2DE).";
    }

    // ── Lifecycle — Launch ────────────────────────────────────────────────────
    // ConnectsItself = true: start the Python client (it owns the slot), then open
    // AoE2 DE. The player enters server/slot/password inside the client.

    public Task LaunchAsync(ApSession session, CancellationToken ct = default)
    {
        _ = session; // connection is entered in the Age2 Client, not via args here

        string? client = ResolveBundledClient();
        if (client != null)
        {
            try { StartBundledClient(client); } catch { /* non-fatal */ }
        }

        try { StartAoE2DE(); } catch { /* non-fatal */ }

        return Task.CompletedTask;
    }

    /// AoE2 DE is a standalone game — plain (non-AP) play is fully supported.
