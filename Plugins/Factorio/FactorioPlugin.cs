    // ── News feed ─────────────────────────────────────────────────────────────
    // Factorio's integration ships with the Archipelago release, so the most honest
    // "news" is the AP release stream (the Factorio Client + generator come from
    // there). Never throws — empty on failure.

    public async Task<NewsItem[]> GetNewsAsync(CancellationToken ct = default)
    {
        try
        {
            string json = await _http.GetStringAsync(ApReleasesApiUrl, ct);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.ValueKind != JsonValueKind.Array)
                return Array.Empty<NewsItem>();

            var items = new List<NewsItem>();
            foreach (var el in doc.RootElement.EnumerateArray())
            {
                DateTimeOffset date = DateTimeOffset.MinValue;
                if (el.TryGetProperty("published_at", out var d) && d.ValueKind == JsonValueKind.String)
                    DateTimeOffset.TryParse(d.GetString(), out date);

                items.Add(new NewsItem(
                    Title:   el.TryGetProperty("name",     out var n) ? n.GetString() ?? "" : "",
                    Body:    el.TryGetProperty("body",     out var b) ? b.GetString() ?? "" : "",
                    Version: el.TryGetProperty("tag_name", out var t) ? t.GetString() ?? "" : "",
                    Date:    date,
                    Url:     el.TryGetProperty("html_url", out var u) ? u.GetString() : null
                ));
                if (items.Count >= 10) break;
            }
            return items.ToArray();
        }
        catch { return Array.Empty<NewsItem>(); }
    }

    // ── Private helpers — Factorio install resolution ─────────────────────────

    /// The Factorio install dir to use: the override (if set and valid) wins, else
    /// the Steam-detected install, else the standalone build under %AppData%. Null
    /// when nothing is found.
    private string? ResolveFactorioDir()
    {
        if (!string.IsNullOrWhiteSpace(_overrideFactorioDir) &&
            LooksLikeFactorioDir(_overrideFactorioDir))
            return _overrideFactorioDir;

        try
        {
            string? steam = DetectSteamFactorioDir();
            if (steam != null) return steam;
        }
        catch { /* ignore — try standalone */ }

        try
        {
            // The standalone build's user-data folder (%AppData%\Factorio) holds
            // mods/saves/config but NOT bin\x64\factorio.exe; the actual program may
            // be anywhere. We only treat %AppData%\Factorio as a Factorio root if the
            // user happens to have extracted the program there too (some do). The
            // override picker is the reliable route for an arbitrary standalone path.
            if (LooksLikeFactorioDir(StandaloneUserDataDir))
                return StandaloneUserDataDir;
        }
        catch { /* ignore */ }

        return null;
    }

    /// The full path to factorio.exe under the resolved Factorio install, or null.
    private string? ResolveFactorioExe()
    {
        string? dir = ResolveFactorioDir();
        if (dir == null) return null;
        string exe = Path.Combine(dir, FactorioExeRelPath);
        return File.Exists(exe) ? exe : null;
    }

    /// A folder "looks like" a Factorio install if bin\x64\factorio.exe exists
    /// under it.
    private static bool LooksLikeFactorioDir(string dir)
    {
        try
        {
            if (string.IsNullOrWhiteSpace(dir) || !Directory.Exists(dir)) return false;
            return File.Exists(Path.Combine(dir, FactorioExeRelPath));
        }
        catch { return false; }
    }

    /// Build the host.yaml-style executable value for guidance text: the resolved
    /// factorio(.exe) path with single backslashes doubled (as the guide requires),
    /// and the ".exe" trimmed (the guide writes "...\\bin\\x64\\factorio").
    private static string FactorioExeForGuidance(string factorioDir)
    {
        string exe = Path.Combine(factorioDir, "bin", "x64", "factorio");
        return exe.Replace("\\", "\\\\");
    }

    /// Detect the Steam Factorio install: read the Steam root from the registry,
    /// gather all library roots from libraryfolders.vdf, and find the one whose
    /// appmanifest_427520.acf exists → steamapps\common\Factorio.
    private static string? DetectSteamFactorioDir()
    {
        foreach (string steamRoot in SteamRoots())
        {
            if (string.IsNullOrWhiteSpace(steamRoot)) continue;
            foreach (string lib in SteamLibraryRoots(steamRoot))
            {
                try
                {
                    string steamapps = Path.Combine(lib, "steamapps");
                    string manifest  = Path.Combine(steamapps, $"appmanifest_{SteamAppId}.acf");
                    if (!File.Exists(manifest)) continue;

                    string common = Path.Combine(steamapps, "common");
                    string? installDir = ReadAcfInstallDir(manifest);
                    if (installDir != null)
                    {
                        string candidate = Path.Combine(common, installDir);
                        if (LooksLikeFactorioDir(candidate)) return candidate;
                    }
                    string conventional = Path.Combine(common, SteamCommonFolderName);
                    if (LooksLikeFactorioDir(conventional)) return conventional;
                }
                catch { /* try the next library */ }
            }
        }
        return null;
    }

    // ── Private helpers — Archipelago install / bundled client (READ-ONLY) ─────

    /// The Archipelago install dir to use (READ-ONLY). The override wins if it
    /// still exists; else the first standard location that exists. We do NOT require
    /// the client exe here (LocateFactorioClientExe does that) — this is just "where
    /// Archipelago is".
    private string? ResolveArchipelagoDir()
    {
        if (!string.IsNullOrWhiteSpace(_overrideArchipelagoDir) &&
            Directory.Exists(_overrideArchipelagoDir))
            return _overrideArchipelagoDir;

        foreach (string dir in StandardArchipelagoDirs())
        {
            try { if (Directory.Exists(dir)) return dir; }
            catch { /* ignore */ }
        }
        return null;
    }

    /// Standard Archipelago install roots to probe (READ-ONLY):
    ///   * %ProgramData%\Archipelago   (the default Windows installer location)
    ///   * %LocalAppData%\Archipelago
    ///   * %ProgramFiles%\Archipelago, %ProgramFiles(x86)%\Archipelago
    private static IEnumerable<string> StandardArchipelagoDirs()
    {
        var seen = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        foreach (string? root in new[]
        {
            Environment.GetFolderPath(Environment.SpecialFolder.CommonApplicationData), // ProgramData
            Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData),
            Environment.GetFolderPath(Environment.SpecialFolder.ProgramFiles),
            Environment.GetFolderPath(Environment.SpecialFolder.ProgramFilesX86),
        })
        {
            if (string.IsNullOrWhiteSpace(root)) continue;
            string dir = Path.Combine(root, "Archipelago");
            if (seen.Add(dir)) yield return dir;
        }
    }

    /// Locate the bundled ArchipelagoFactorioClient.exe (READ-ONLY scan). Checks the
    /// resolved Archipelago dir first, then every standard location. Returns the exe
    /// path or null. NEVER writes anything while searching.
    private string? LocateFactorioClientExe()
    {
        // 1. The resolved/override Archipelago dir.
        string? apDir = ResolveArchipelagoDir();
        if (apDir != null)
        {
            string? hit = FindFactorioClientExeIn(apDir);
            if (hit != null) return hit;
        }

        // 2. Every other standard location (in case the override points elsewhere).
        foreach (string dir in StandardArchipelagoDirs())
        {
            if (apDir != null && string.Equals(dir, apDir, StringComparison.OrdinalIgnoreCase))
                continue;
            string? hit = FindFactorioClientExeIn(dir);
            if (hit != null) return hit;
        }
        return null;
    }

    /// Find ArchipelagoFactorioClient.exe inside `dir`: prefer the exact name at the
    /// root, else a fuzzy "*factorio*client*" / "*factorioclient*" exe anywhere under
    /// it. READ-ONLY (enumeration only); all failures degrade to null.
    private static string? FindFactorioClientExeIn(string dir)
    {
        try
        {
            if (string.IsNullOrWhiteSpace(dir) || !Directory.Exists(dir)) return null;

            string preferred = Path.Combine(dir, PreferredClientExeName);
            if (File.Exists(preferred)) return preferred;

            foreach (string exe in Directory.EnumerateFiles(dir, "*.exe", SearchOption.AllDirectories))
            {
                string name = Path.GetFileNameWithoutExtension(exe).ToLowerInvariant();
                if (name.Contains("factorio") && name.Contains("client"))
                    return exe;
            }
        }
        catch { /* permission / vanished — null (never throws into a scan) */ }
        return null;
    }

    // ── Private helpers — launch ──────────────────────────────────────────────

    /// Launch the bundled Factorio Client (the AP entry point). If it cannot be
    /// located, throw with honest guidance so the UI surfaces the real next step.
    private void LaunchFactorioClient()
    {
        string? exe = LocateFactorioClientExe();
        if (exe == null)
            throw new FileNotFoundException(
                "Archipelago's bundled Factorio Client (ArchipelagoFactorioClient.exe) was not " +
                "found. Install Archipelago and check the \"Factorio Client\" during setup, then " +
                "point this plugin at your Archipelago folder in Settings. Then run the client and " +
                "type  /connect <server:port>.",
                PreferredClientExeName);

        var proc = Process.Start(new ProcessStartInfo
        {
            FileName         = exe,
            WorkingDirectory = Path.GetDirectoryName(exe) ?? "",
            UseShellExecute  = true,   // the client may attach a console window of its own
        }) ?? throw new InvalidOperationException("Failed to start the Factorio Client.");

        _clientProcess = proc;
        IsRunning      = true;
        try
        {
            proc.EnableRaisingEvents = true;
            proc.Exited += (_, _) =>
            {
                IsRunning = false;
                GameExited?.Invoke(proc.ExitCode);
            };
        }
        catch { /* some processes don't expose Exited — non-fatal */ }
    }

    /// Launch Factorio itself (standalone / non-AP play): prefer bin\x64\factorio.exe
    /// in the resolved install; if that cannot be found but Steam is present, fall
    /// back to the steam:// URL. Surfaces a clear message rather than failing opaquely.
    private void LaunchFactorioGame()
    {
        string? exe = ResolveFactorioExe();
        if (exe != null && File.Exists(exe))
        {
            var proc = Process.Start(new ProcessStartInfo
            {
                FileName         = exe,
                WorkingDirectory = Path.GetDirectoryName(exe) ?? "",
                UseShellExecute  = true,
            }) ?? throw new InvalidOperationException("Failed to start Factorio.");

            _clientProcess = proc;
            IsRunning      = true;
            try
            {
                proc.EnableRaisingEvents = true;
                proc.Exited += (_, _) =>
                {
                    IsRunning = false;
                    GameExited?.Invoke(proc.ExitCode);
                };
            }
            catch { /* non-fatal */ }
            return;
        }

        // Fall back to Steam if we at least know Steam is installed.
        if (SteamRoots().Any(r => !string.IsNullOrWhiteSpace(r) && Directory.Exists(r)))
        {
            try
            {
                Process.Start(new ProcessStartInfo(SteamRunUrl) { UseShellExecute = true });
                IsRunning = true; // best-effort; Steam owns the process, so we won't track exit
                return;
            }
            catch { /* fall through to the error below */ }
        }

        throw new FileNotFoundException(
            "Could not find Factorio (bin\\x64\\factorio.exe). Open this game's Settings and pick " +
            "your Factorio install folder, or install Factorio via Steam.",
            "factorio.exe");
    }

    // ── Private helpers — Steam detection (adapted from the Subnautica plugin) ──

    /// Candidate Steam install roots from the registry (HKCU SteamPath +
    /// HKLM WOW6432Node/Valve InstallPath), plus the conventional Program Files (x86)
    /// location. Duplicates are harmless (callers de-dup).
    private static IEnumerable<string> SteamRoots()
    {
        string? hkcu = ReadRegistryString(Registry.CurrentUser,
            @"Software\Valve\Steam", "SteamPath");
        if (!string.IsNullOrWhiteSpace(hkcu)) yield return NormalizeSteamPath(hkcu);

        string? hklm = ReadRegistryString(Registry.LocalMachine,
            @"SOFTWARE\WOW6432Node\Valve\Steam", "InstallPath");
        if (!string.IsNullOrWhiteSpace(hklm)) yield return NormalizeSteamPath(hklm);

        string? hklm64 = ReadRegistryString(Registry.LocalMachine,
            @"SOFTWARE\Valve\Steam", "InstallPath");
        if (!string.IsNullOrWhiteSpace(hklm64)) yield return NormalizeSteamPath(hklm64);

        string? progX86 = Environment.GetFolderPath(Environment.SpecialFolder.ProgramFilesX86);
        if (!string.IsNullOrWhiteSpace(progX86))
            yield return Path.Combine(progX86, "Steam");
    }

    /// Steam stores its SteamPath with forward slashes; normalize for Path APIs.
    private static string NormalizeSteamPath(string p)
        => p.Replace('/', '\\').TrimEnd('\\');

    /// All Steam library roots: the Steam root itself plus every "path" entry in
    /// steamapps\libraryfolders.vdf. Tolerant text scan.
    private static IEnumerable<string> SteamLibraryRoots(string steamRoot)
    {
        var seen = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
        if (seen.Add(steamRoot)) yield return steamRoot;

        string vdf = Path.Combine(steamRoot, "steamapps", "libraryfolders.vdf");
        if (!File.Exists(vdf)) yield break;

        string text;
        try { text = File.ReadAllText(vdf); }
        catch { yield break; }

        foreach (string path in ExtractVdfPaths(text))
        {
            string norm = path.Replace('/', '\\').TrimEnd('\\');
            if (norm.Length > 0 && seen.Add(norm))
                yield return norm;
        }
    }

    /// Pull every  "path"   "<value>"  pair out of a libraryfolders.vdf body.
    /// Handles the Steam-VDF escaping of backslashes (\\ → \).
    private static IEnumerable<string> ExtractVdfPaths(string text)
    {
        const string key = "\"path\"";
        int i = 0;
        while ((i = text.IndexOf(key, i, StringComparison.OrdinalIgnoreCase)) >= 0)
        {
            i += key.Length;
            int open = text.IndexOf('"', i);
            if (open < 0) yield break;
            int close = text.IndexOf('"', open + 1);
            if (close < 0) yield break;

            string raw = text.Substring(open + 1, close - open - 1);
            yield return raw.Replace("\\\\", "\\");
            i = close + 1;
        }
    }

    /// Read the "installdir" value from an appmanifest_*.acf (same quoted-pair
    /// format as VDF). Returns null if absent.
    private static string? ReadAcfInstallDir(string acfPath)
    {
        try
        {
            string text = File.ReadAllText(acfPath);
            const string key = "\"installdir\"";
            int i = text.IndexOf(key, StringComparison.OrdinalIgnoreCase);
            if (i < 0) return null;
            i += key.Length;
            int open = text.IndexOf('"', i);
            if (open < 0) return null;
            int close = text.IndexOf('"', open + 1);
            if (close < 0) return null;
            return text.Substring(open + 1, close - open - 1).Replace("\\\\", "\\");
        }
        catch { return null; }
    }

    /// Safe registry string read; null on any failure (key/value missing, etc.).
    private static string? ReadRegistryString(RegistryKey hive, string subKey, string valueName)
    {
        try
        {
            using RegistryKey? k = hive.OpenSubKey(subKey);
            return k?.GetValue(valueName) as string;
        }
        catch { return null; }
    }

