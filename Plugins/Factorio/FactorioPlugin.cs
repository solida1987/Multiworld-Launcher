// ═══════════════════════════════════════════════════════════════════════════════
// FactorioPlugin — detect / guide / launch for "Factorio" (Wube Software) played
// through its OFFICIAL Archipelago integration. NATIVE "ConnectsItself" — the AP
// slot connection is owned by a SEPARATE bundled tool (the Factorio Client), not
// by the game process and not by the launcher, exactly like the shipped Undertale
// plugin (an external-client honest case).
//
// WHAT KIND OF INTEGRATION IS THIS? (verified this session — see REALITY CHECK)
// ─────────────────────────────────────────────────────────────────────────────
// Factorio's Archipelago support is part of AP-MAIN itself (worlds/factorio), NOT
// a third-party mod repo with a one-click download. The moving pieces are:
//
//   * The "Factorio Client" — a Python client that SHIPS INSIDE the Archipelago
//     install the player already has (registered as
//     Component("Factorio Client", func=launch_client, component_type=Type.CLIENT)
//     in worlds/factorio/__init__.py; on Windows the bundled installer produces
//     ArchipelagoFactorioClient.exe). It OWNS the AP slot connection: it launches
//     a Factorio *server* with the generated AP mod and relays between that server
//     and the Archipelago multiworld.
//   * The player's OWN Factorio (Steam appid 427520, or the standalone build from
//     factorio.com). The client needs the path to that game's
//     bin\x64\factorio(.exe) set in its host.yaml (factorio_options: executable).
//   * The per-seed AP Factorio mod (an "AP_*.zip"), generated by the multiworld,
//     which goes into Factorio's mods folder (%AppData%\Factorio\mods for the
//     client's own Factorio, or the dedicated server's mods folder).
//
// HONESTY NOTE — this is a GUIDED case (like Undertale / Ship of Harkinian), and
// like Undertale the AP slot is held by a SEPARATE long-running tool (the Factorio
// Client), not by the game process. The concrete, verified reasons the launcher
// canNOT do a fake "one-click install":
//
//   1. There is NO redistributable runnable game and NO standalone mod zip the
//      launcher may ship: Factorio is paid software (Steam / factorio.com), and the
//      client + generator that drive it live INSIDE the player's Archipelago
//      install. The launcher must not, and does not, ship or recreate either.
//   2. The OFFICIAL, documented setup (the AP "Factorio" setup guide) is:
//        a. Install a DEDICATED, isolated Factorio for the client to control
//           (standalone build recommended; a second copy is needed if you also
//           host on the same machine).
//        b. In ArchipelagoFactorioClient's host.yaml, set
//             factorio_options:
//               executable: C:\\path\\to\\factorio\\bin\\x64\\factorio
//           (double backslashes required).
//        c. Put the generated AP_*.zip mod into the Factorio mods folder
//           (%AppData%\Factorio\mods, or the server's mods folder).
//        d. Run ArchipelagoFactorioClient.exe and type  /connect <server:port>
//           into its input box.
//      Reproducing that (download a multi-hundred-MB game, edit the client's
//      host.yaml, drop a per-seed mod, drive the client) is exactly the bundled
//      client's job — so, like Undertale did not reproduce /auto_patch, this
//      plugin does NOT fake it.
//   3. CONNECTION IS CLIENT-OWNED, NOT in-game and NOT launcher-relay. The launcher
//      cannot pass connection args to the game and must NOT hold its own ApClient
//      on the slot (the Factorio Client would be kicked, or kick us). Hence
//      ConnectsItself = true.
//
// WHAT THIS PLUGIN HONESTLY DOES:
//   * DETECT the player's Factorio:
//       - Steam appid 427520 via registry → libraryfolders.vdf →
//         appmanifest_427520.acf → steamapps\common\Factorio (the same pipeline the
//         shipped Subnautica/Stardew plugins use), and
//       - the standalone build: %AppData%\Factorio (Roaming user data) and a
//         user-set install-folder override.
//     so it can tell the player exactly where bin\x64\factorio(.exe) lives.
//   * LOCATE the Archipelago install (READ-ONLY) to find the bundled client
//     ArchipelagoFactorioClient.exe — searching %ProgramData%\Archipelago,
//     %LocalAppData%\Archipelago, %ProgramFiles%\Archipelago, and a user-set
//     Archipelago-folder override.
//     ***CRITICAL SECURITY: C:\ProgramData\Archipelago is treated as READ-ONLY.
//        This plugin only READS it to find the bundled client. It NEVER writes,
//        creates, deletes, or modifies anything under any Archipelago install.***
//   * GUIDE the irreducible steps in InstallOrUpdate / Settings (get Factorio, set
//     the client's host.yaml executable, drop the AP mod, run the client and
//     /connect), with direct links (Factorio download/Steam page, AP releases, the
//     official setup guide, archipelago.gg).
//   * LAUNCH best-effort: start ArchipelagoFactorioClient.exe (the bundled client
//     owns the connection — the player then types /connect <server:port>). The
//     session's server/slot are surfaced in the panel/notes to copy. If the client
//     cannot be located, fail with honest guidance instead of a fake success.
//     SupportsStandalone = true launches Factorio itself (bin\x64\factorio.exe, or
//     steam://rungameid/427520) for plain, non-AP play.
//   * NEVER claim an install exists when it does not, never modify the player's
//     Factorio or Archipelago installs (§11 + the read-only rule above), and keep
//     its settings in its OWN JSON sidecar (does not touch Core/SettingsStore).
//
// REALITY CHECK (2026-06-14) — facts verified this session against AP-main + the
// official setup guide (https://archipelago.gg/tutorial/Factorio/setup_en):
//   * AP game string: "Factorio" — VERIFIED in worlds/factorio/__init__.py
//     (FactorioWorld.game = "Factorio"). World id "factorio".
//   * Client component: Component("Factorio Client", func=launch_client,
//     component_type=Type.CLIENT) in worlds/factorio/__init__.py; the Windows
//     bundled exe is ArchipelagoFactorioClient.exe (installed with Archipelago if
//     the client was selected during install).
//   * Steam appid 427520 (store.steampowered.com/app/427520).
//   * factorio(.exe) lives at bin\x64\factorio(.exe) in every Factorio install
//     (Steam and standalone). Standalone user data (mods/saves/config) is at
//     %AppData%\Factorio (Roaming).
//   * host.yaml (next to the client): factorio_options: executable:
//     C:\\path\\to\\factorio\\bin\\x64\\factorio  (double backslashes required).
//   * Mod: the generated AP_*.zip goes into the Factorio mods folder
//     (%AppData%\Factorio\mods, or the dedicated server's mods folder).
//   * Connect: run ArchipelagoFactorioClient.exe, type  /connect <server:port>.
//
// DEFENSIVE / UNVERIFIED DETAILS (flagged, "verify at build time", Undertale-style):
//   * The bundled client exe name is "ArchipelagoFactorioClient.exe" on Windows
//     (verified from the setup guide). LocateFactorioClientExe() prefers that exact
//     name, then a fuzzy "*factorio*client*.exe" / "*factorioclient*.exe" anywhere
//     under the resolved Archipelago install, so a differently-cased build still
//     resolves. If the user installed Archipelago without the Factorio client, no
//     exe is found and the plugin stays HONEST (guides the user to install it /
//     point at it). It NEVER writes into the Archipelago folder while searching.
//   * "IsInstalled" here means BOTH halves are present: a Factorio executable is
//     resolvable AND the bundled ArchipelagoFactorioClient.exe is locatable. The
//     game alone, or the client alone, is not enough to play, so neither flips it
//     true by itself.
//   * The launcher cannot verify the user actually edited host.yaml or dropped the
//     AP mod (those live inside the read-only Archipelago install / the player's
//     Factorio mods folder, which it does not modify); the Settings panel is
//     explicit about those remaining manual steps.
// ═══════════════════════════════════════════════════════════════════════════════

