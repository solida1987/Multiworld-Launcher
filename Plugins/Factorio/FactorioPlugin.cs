    // ── Lifecycle — InstallOrUpdate ───────────────────────────────────────────
    // HONEST guided setup. There is nothing for the launcher to download here (the
    // Factorio game is the player's paid copy, and the client + generator + the
    // per-seed mod come from the player's Archipelago install / the multiworld). So
    // this reports the current detection state and the exact remaining manual steps
    // — it never fabricates an install, and it never writes to the Archipelago
    // folder (read-only).

    public Task InstallOrUpdateAsync(
        IProgress<(int Pct, string Msg)> progress,
        CancellationToken ct = default)
    {
        progress.Report((10, "Looking for your Factorio installation..."));
        string? factorioDir = ResolveFactorioDir();

        progress.Report((45, "Looking for Archipelago's bundled Factorio Client..."));
        string? clientExe = LocateFactorioClientExe();

        bool haveGame   = factorioDir != null;
        bool haveClient = clientExe   != null;

        if (haveGame && haveClient)
        {
            progress.Report((100,
                $"Ready: Factorio found at \"{factorioDir}\" and the bundled client at " +
                $"\"{clientExe}\". One-time setup if you have not done it: in the client's " +
                "host.yaml set  factorio_options: executable  to  " +
                $"\"{FactorioExeForGuidance(factorioDir!)}\"  (double backslashes), and put " +
                "your generated AP_*.zip mod into Factorio's mods folder " +
                $"(\"{Path.Combine(StandaloneUserDataDir, "mods")}\"). Press Play to start " +
                "the client, then type  /connect <server:port>  in it. See the Setup Guide " +
                "link in Settings."));
            return Task.CompletedTask;
        }

        // Give the most specific guidance we can for whatever is missing.
        var sb = new StringBuilder();
        if (!haveGame)
            sb.Append("Factorio was not detected. Install it (Steam appid ").Append(SteamAppId)
              .Append(", or the standalone build from factorio.com), then point this plugin at " +
                      "the Factorio folder (the one containing bin\\x64\\factorio.exe) in Settings. ");
        if (!haveClient)
            sb.Append("Archipelago's bundled \"Factorio Client\" (ArchipelagoFactorioClient.exe) " +
                      "was not found. Install Archipelago and CHECK the Factorio client during " +
                      "setup, then point this plugin at your Archipelago folder in Settings. ");
        sb.Append("Then in the client's host.yaml set  factorio_options: executable  to your " +
                  "Factorio bin\\x64\\factorio (double backslashes), drop your generated AP_*.zip " +
                  "mod into Factorio's mods folder, run the client, and type  /connect " +
                  "<server:port>. See the Setup Guide link in Settings.");

        progress.Report((100, sb.ToString()));
        return Task.CompletedTask;
    }

    // ── Lifecycle — Verify ────────────────────────────────────────────────────

    public Task<bool> VerifyInstallAsync(CancellationToken ct = default)
        => Task.FromResult(IsInstalled);

    // ── AutoMod-style validation of a user-picked Factorio folder ─────────────

    /// Used by the Settings folder picker for the FACTORIO install. Accept it only
    /// when it contains bin\x64\factorio.exe (or the user picked a parent we can
    /// descend into). Returns null when acceptable, else a short reason.
    public string? ValidateExistingInstall(string folder)
    {
        if (string.IsNullOrWhiteSpace(folder) || !Directory.Exists(folder))
            return "That folder does not exist. Pick your Factorio install folder " +
                   "(the one containing bin\\x64\\factorio.exe).";

        if (LooksLikeFactorioDir(folder))
            return null;

        // Be forgiving: the user may have picked the Steam "common" parent.
        try
        {
            string nested = Path.Combine(folder, SteamCommonFolderName);
            if (LooksLikeFactorioDir(nested)) return null;
        }
        catch { /* ignore */ }

        return "That does not look like a Factorio installation. Pick the folder that " +
               "contains bin\\x64\\factorio.exe (for Steam this is usually " +
               @"...\steamapps\common\Factorio).";
    }

    // ── Lifecycle — Launch ────────────────────────────────────────────────────
    // Best effort: start the bundled Factorio Client so the player gets one-click
    // launch. The connection itself is entered into that client (type
    // /connect <server:port>), so we do NOT pass connection args and we do NOT hold
    // an ApClient on the slot (ConnectsItself = true). If the client cannot be
    // located, fail with honest guidance.

    public Task LaunchAsync(ApSession session, CancellationToken ct = default)
    {
        _ = session; // connection is made in the Factorio Client, not via args here
        LaunchFactorioClient();
        return Task.CompletedTask;
    }

    /// Factorio is a complete game — plain (non-AP) play is possible (launch the
    /// game itself rather than the AP client).
    public bool SupportsStandalone => true;

    /// The external Factorio Client owns the AP slot connection (see header). The
    /// launcher must NOT connect its own ApClient to the same slot while the client
    /// runs, or they would kick each other off.
    public bool ConnectsItself => true;

    public Task LaunchStandaloneAsync(CancellationToken ct = default)
    {
        LaunchFactorioGame();
        return Task.CompletedTask;
    }

    public Task StopAsync()
    {
        // We only ever started the bundled Factorio Client (or the game) from here.
        // Kill what WE launched — never the user's AP server / other processes.
        try { _clientProcess?.Kill(entireProcessTree: true); } catch { }
        IsRunning = false;
        // No password/args are written to disk by this plugin (the connection is
        // entered in the client), so there is nothing sensitive to scrub.
        _clientProcess = null;
        return Task.CompletedTask;
    }

    // ── AP bridge — inert (see header) ────────────────────────────────────────

