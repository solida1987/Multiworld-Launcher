    // ── Lifecycle — CheckForUpdate ────────────────────────────────────────────

    public async Task CheckForUpdateAsync(CancellationToken ct = default)
    {
        try
        {
            InstalledVersion = File.Exists(VersionFilePath) && IsInstalled
                ? (await File.ReadAllTextAsync(VersionFilePath, ct)).Trim()
                : (IsInstalled ? "installed" : null);
        }
        catch { InstalledVersion = null; }

        try
        {
            // CDN HEAD redirect — no REST API quota consumed.
            AvailableVersion = GitHubHelper.NormalizeTag(
                await GitHubHelper.FetchLatestTagAsync(WWRANDO_OWNER, WWRANDO_REPO, ct));
        }
        catch { AvailableVersion = null; }
    }

    // ── Lifecycle — InstallOrUpdate ───────────────────────────────────────────

    public async Task InstallOrUpdateAsync(
        IProgress<(int Pct, string Msg)> progress,
        CancellationToken ct = default)
    {
        progress.Report((2, "Checking latest wwrando (AP fork) release..."));
        var (version, zipUrl, zipName) = await ResolveLatestWwrandoAsync(ct);
        AvailableVersion = version;

        // Already current?
        if (IsInstalled
            && File.Exists(VersionFilePath)
            && (await File.ReadAllTextAsync(VersionFilePath, ct)).Trim() == version)
        {
            InstalledVersion = version;
            progress.Report((100, $"wwrando {version} is up to date."));
            return;
        }

        if (zipUrl == null)
            throw new InvalidOperationException(
                "Could not find the wwrando Windows download on GitHub. Check your " +
                "internet connection or download manually from " +
                WwrandoRepoUrl + "/releases (look for a tag starting with \"ap_\"). " +
                "You also need Dolphin (https://dolphin-emu.org/download/) and the " +
                "Archipelago Launcher (https://archipelago.gg/). Read the setup guide: " +
                SetupGuideUrl);

        await DownloadAndExtractWwrandoAsync(zipUrl, zipName ?? FallbackWwrandoZipName,
            version, progress, ct);

        await File.WriteAllTextAsync(VersionFilePath, version, ct);
        InstalledVersion = version;

        Directory.CreateDirectory(PatchedIsoDirectory);

        progress.Report((100,
            $"wwrando {version} installed. " +
            "NEXT STEPS: (1) Make sure Dolphin is installed. " +
            "(2) Make sure the Archipelago Launcher is installed from archipelago.gg. " +
            "(3) Use Settings to point to your Wind Waker GCN ISO (North American .iso). " +
            "(4) When you have a .aptww file from your AP multiworld, open wwrando " +
            "(Launch > Open Patcher), load the .aptww and your ISO, and click Randomize " +
            "to produce the patched ISO. Then click Play to start the TWW Client and Dolphin."));
    }

    // ── Lifecycle — Verify ────────────────────────────────────────────────────

