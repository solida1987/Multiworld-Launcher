        await Task.CompletedTask;
        return IsInstalled;
    }

    // ── Lifecycle — Launch ────────────────────────────────────────────────────

    /// ConnectsItself = true: the TWW Client (via AP Launcher) holds the AP slot.
    public bool ConnectsItself     => true;

    /// No standalone non-AP play via this integration (wwrando is for AP seeds).
    public bool SupportsStandalone => false;

    public Task LaunchAsync(ApSession session, CancellationToken ct = default)
    {
        // Step 1: warn if no patched ISO is available.
        string? patchedIso = FindMostRecentPatchedIso();
        if (patchedIso == null)
        {
            MessageBox.Show(
                "No patched Wind Waker ISO found in:\n" + PatchedIsoDirectory + "\n\n" +
                "Before launching, you must patch your vanilla ISO with wwrando:\n" +
                "  1. Open wwrando (Install > Open Patcher on this tab).\n" +
                "  2. Select your .aptww file (from the AP multiworld organizer).\n" +
                "  3. Select your vanilla Wind Waker ISO (North American).\n" +
                "  4. Click Randomize. The patched ISO is saved in the output folder.\n\n" +
                "Then click Play again.",
                "Patch Required",
                MessageBoxButton.OK,
                MessageBoxImage.Information);
            return Task.CompletedTask;
        }

        // Step 2: start the TWW Client via ArchipelagoLauncher.exe.
        StartTwwClient(session);

        // Step 3: start Dolphin with the patched ISO.
        StartDolphin(patchedIso);

        return Task.CompletedTask;
    }

    public Task LaunchStandaloneAsync(CancellationToken ct = default)
    {
        // No standalone mode — the integration only makes sense with AP.
        MessageBox.Show(
            "The Wind Waker AP integration requires an Archipelago multiworld session " +
            "and a patched ISO generated by wwrando. There is no standalone (non-AP) play " +
            "through this launcher entry.\n\n" +
            "To open wwrando's patcher, use the Open Patcher button in Settings.",
            "No Standalone Mode",
            MessageBoxButton.OK,
            MessageBoxImage.Information);
        return Task.CompletedTask;
    }

    public Task StopAsync()
    {
        try { _twwClientProcess?.Kill(entireProcessTree: true); } catch { }
        try { _dolphinProcess?.Kill(entireProcessTree: true); } catch { }
        IsRunning         = false;
        _twwClientProcess = null;
        _dolphinProcess   = null;
        return Task.CompletedTask;
    }

