    /// Scan common Dolphin install locations on Windows. Returns the first exe
    /// found, or null. Never throws.
    private static string? DetectDolphinExe()
    {
        string[] exeNames = { "Dolphin.exe", "DolphinQt2.exe" };

        string localApp = Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData);
        string progFiles = Environment.GetFolderPath(Environment.SpecialFolder.ProgramFiles);
        string progX86   = Environment.GetFolderPath(Environment.SpecialFolder.ProgramFilesX86);

        string[] searchRoots =
        {
            Path.Combine(localApp,   "Dolphin"),
            Path.Combine(localApp,   "Programs", "Dolphin"),
            Path.Combine(progFiles,  "Dolphin"),
            Path.Combine(progFiles,  "Dolphin Emulator"),
            Path.Combine(progX86,    "Dolphin"),
            Path.Combine(progX86,    "Dolphin Emulator"),
        };

        foreach (string root in searchRoots)
        {
            if (!Directory.Exists(root)) continue;
            foreach (string name in exeNames)
            {
                string candidate = Path.Combine(root, name);
                if (File.Exists(candidate)) return candidate;
            }
            // Also walk one level deep (e.g. Dolphin\Dolphin-x64\Dolphin.exe)
            try
            {
                foreach (string sub in Directory.EnumerateDirectories(root))
                {
                    foreach (string name in exeNames)
                    {
                        string candidate = Path.Combine(sub, name);
                        if (File.Exists(candidate)) return candidate;
                    }
                }
            }
            catch { /* skip */ }
        }
        return null;
    }

