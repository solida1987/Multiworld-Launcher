    /// Start the TWW Client via ArchipelagoLauncher.exe. Passes "The Wind Waker
    /// Client" as the client name and --connect / --password from the session.
    /// If the launcher opens a menu instead of auto-selecting, the user picks
    /// the client there. Best effort — does not block launch if AP is absent.
    private void StartTwwClient(ApSession session)
    {
        string? launcherExe = FindApLauncherExe();
        if (launcherExe == null)
        {
            MessageBox.Show(
                "Archipelago Launcher not found in " + ApProgramDataDir + ".\n\n" +
                "Please install Archipelago from https://archipelago.gg to get the " +
                "Wind Waker Client. Without it, Dolphin will launch but location " +
                "checks will not be tracked.",
                "AP Launcher Not Found",
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
            return;
        }

        // Build args: "The Wind Waker Client" --connect <server> [--password <pw>]
        var args = new List<string> { $"\"{TwwClientName}\"" };
        if (!string.IsNullOrWhiteSpace(session.ServerUri))
            args.Add($"--connect \"{session.ServerUri}\"");
        if (!string.IsNullOrWhiteSpace(session.Password))
            args.Add($"--password \"{session.Password}\"");

        try
        {
            var proc = Process.Start(new ProcessStartInfo
            {
                FileName         = launcherExe,
                Arguments        = string.Join(" ", args),
                WorkingDirectory = Path.GetDirectoryName(launcherExe) ?? ApProgramDataDir,
                UseShellExecute  = false,
            });
            if (proc != null)
            {
                _twwClientProcess = proc;
                IsRunning = true;
                try
                {
                    proc.EnableRaisingEvents = true;
                    proc.Exited += (_, _) =>
                    {
                        IsRunning = false;
                        GameExited?.Invoke(proc.ExitCode);
                    };
                }
                catch { /* non-fatal */ }
            }
        }
        catch (Exception ex)
        {
            MessageBox.Show(
                "Could not start the Archipelago Launcher:\n" + ex.Message +
                "\n\nDolphin will still be launched. Start the TWW Client manually " +
                "from the Archipelago Launcher if needed.",
                "Client Launch Error",
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
        }
    }

