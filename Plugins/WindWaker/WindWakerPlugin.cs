// WindWakerPlugin — install / patch / launch for "The Wind Waker" — the
// Nintendo GameCube game played through the Archipelago multiworld.
//
// ── HONEST REALITY CHECK (2026-06-14, verified against AP main repo + tanjo3) ─
//
//   * AP WORLD — game string "The Wind Waker" (verified: worlds/tww/__init__.py,
//     game = "The Wind Waker"). GameId = "tww". AP-main (ArchipelagoMW) in-tree.
//     No separate apworld to distribute — it ships with AP itself.
//
//   * THREE COMPONENTS — verified by examining worlds/tww/TWWClient.py +
//     tanjo3/wwrando + the official setup guide (archipelago.gg/tutorial/
//     The%20Wind%20Waker/setup/en):
//
//     1. wwrando (tanjo3/wwrando, "archipelago" branch / "ap_2.x" releases)
//        A fork of the Wind Waker Randomizer with AP support. This is a SEPARATE
//        Windows application (Qt-based GUI; ships as wwrando_ap-<ver>-windows.zip
//        containing a wwrando_ap.exe or similar). It takes the user's vanilla GCN
//        ISO and an `.aptww` patch file (generated by the AP server) and produces
//        a PATCHED ISO that the emulator runs. The ".aptww" is passed as
//        "plando_file" in the GUI — there are no verified batch-mode / silent-
//        patch CLI args, so this plugin opens wwrando's GUI and instructs the
//        user to load their .aptww + ISO and click Randomize. Latest AP-flavour
//        release: tag "ap_2.5.1" (2026-04-05), Windows asset
//        "wwrando_ap-2.5.1-windows.zip" (verified). Pinned fallback used when the
//        GitHub API is unreachable.
//
//     2. Dolphin emulator — standard GameCube emulator. The user installs it
//        themselves (latest official release at dolphin-emu.org). This plugin
//        detects common installation paths for the launcher's Dolphin path hint
//        but does NOT download or manage Dolphin (it is a large, well-known, self-
//        updating application). The patched ISO is launched via Dolphin's command
//        line: Dolphin.exe -b -e "<patchedIso>". For Dolphin 5.x+ the flag is
//        "-b -e <iso>"; for builds that renamed the exe to "DolphinQt2.exe" both
//        names are tried.
//
//     3. TWW Client — this is a PYTHON CLIENT BUNDLED IN ARCHIPELAGO ITSELF
//        (worlds/tww/TWWClient.py, launched via ArchipelagoLauncher.exe as "The
//        Wind Waker Client"). It uses dolphin_memory_engine to hook Dolphin,
//        reads the slot name from GC RAM (SLOT_NAME_ADDR = 0x803FE8A0) and auto-
//        authenticates, then sends location checks and delivers items. Verified
//        CLI (from TWWClient.py main()): the standard AP get_base_parser()
//        arguments:
//            ArchipelagoLauncher.exe "The Wind Waker Client"
//                --connect <server:port>
//                --password <pw>
//        (server/password optional — the client's GUI accepts them too).
//        "ArchipelagoLauncher.exe" lives in C:\ProgramData\Archipelago (READ-ONLY
//        for our launcher — we detect it but never write there). No standalone
//        TWW Client exe exists; the client is part of the AP installation.
//
//   * ISO VALIDATION — The Wind Waker North American GameCube ISO:
//       Game ID bytes 0x000000: "GZLE99" (6 bytes, ASCII) — this exact check is
//       VERIFIED from TWWClient.py line:
//           dolphin_memory_engine.read_bytes(0x80000000, 6) != b"GZLE99"
//       The disc header at offset 0 in a GCN ISO is the 4-byte game ID plus
//       region/maker, making "GZLE99" the canonical identification. We validate
//       by reading the first 6 bytes of the ISO file. (RAM base 0x80000000 maps
//       to the disc header in Dolphin memory — we read from the ISO file itself,
//       not from RAM, so we read ISO bytes 0–5 directly.) A valid GCN ISO is at
//       least 512 MB; we accept 400 MB–4 GB to accommodate compressed images.
//
//   * GCN ISO FORMAT — GameCube ISOs (also called .gcm) are raw disc images.
//       Extension: .iso or .gcm. The disc header starts at byte 0 in the image.
//       Standard TWW (NA) disc ID: "GZLE" + maker "99" = "GZLE99".
//
//   * ConnectsItself = true — the TWW Client (via AP Launcher) owns the AP slot.
//     SupportsStandalone = false — the game requires the wwrando patch + Dolphin,
//     and there is no meaningful "play without AP" flow in this integration.
//
//   * PATCHED ISO LIFETIME — The patched ISO is generated in the user's chosen
//     output folder (default: Games/ROMs/tww/patched/). It is specific to the
//     AP seed/slot. On launch we look for the most recently generated .iso/.gcm
//     in that folder.
//
// ── WHAT THIS PLUGIN DOES ─────────────────────────────────────────────────────
//   1. INSTALL wwrando (AP fork) from tanjo3/wwrando GitHub releases. Downloads
//      the Windows zip, extracts into Games/TWWRando/. Stamps version.
//   2. SETTINGS:
//      - ISO path picker (validates "GZLE99" magic + size), copies to ROM library
//        (original never modified), stores path in sidecar JSON.
//      - wwrando install path (defaults to Games/TWWRando/, user can override).
//      - Dolphin path picker (auto-detect from common paths, user can override).
//      - aptww file path picker (copied to Games/ROMs/tww/ for convenience).
//   3. PATCH: guides user to open wwrando with their ISO + .aptww file, or opens
//      wwrando GUI with a hint about the files (we cannot inject aptww/iso args
//      silently since wwrando's no-ui mode reads from its own saved settings,
//      not from CLI positional args). The post-launch note instructs the user.
//   4. LAUNCH (AP):
//      a. Warn if no patched ISO exists yet (user must patch with wwrando first).
//      b. Detect ArchipelagoLauncher.exe in ProgramData\Archipelago (READ-ONLY).
//         If found, launch it with "The Wind Waker Client" --connect <server>
//         --password <pw>. If not found, launch ArchipelagoLauncher.exe with
//         those args anyway (user must install AP).
//      c. Launch Dolphin with the patched ISO: Dolphin.exe -b -e "<iso>".
//      The order: Client first, then Dolphin (the client waits for Dolphin to
//      hook anyway per its connection loop).
//   5. News from tanjo3/wwrando releases.
//
// ── DEFENSIVE / UNVERIFIED ("verify at build time") ───────────────────────────
//   * wwrando Windows exe name inside the zip: likely "wwrando_ap.exe" or
//     similar. Resolution is fuzzy ("*wwrando*", "*rando*", first non-installer
//     exe) — verified at actual build time when the zip is extracted.
//   * Dolphin exe name: "Dolphin.exe" or "DolphinQt2.exe". Both are tried.
//   * ArchipelagoLauncher.exe args for TWW Client: standard AP launcher usage
//     "The Wind Waker Client" --connect <server> --password <pw> — matches the
//     get_base_parser() pattern. If the launcher UI ignores the client-name arg
//     and opens a menu, the user picks "The Wind Waker Client" from it manually.
// ═══════════════════════════════════════════════════════════════════════════════

