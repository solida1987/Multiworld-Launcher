    public async Task<NewsItem[]> GetNewsAsync(CancellationToken ct = default)
    {
        try
        {
            string json = await _http.GetStringAsync(GH_WWRANDO_RELEASES_URL, ct);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.ValueKind != JsonValueKind.Array)
                return Array.Empty<NewsItem>();

            var items = new List<NewsItem>();
            foreach (var el in doc.RootElement.EnumerateArray())
            {
                string? tag = el.TryGetProperty("tag_name", out var t) ? t.GetString() : null;
                // Only include AP-flavoured releases (tag starts with "ap_" or "ap").
                if (tag == null || (!tag.StartsWith("ap", StringComparison.OrdinalIgnoreCase)))
                    continue;

                DateTimeOffset date = DateTimeOffset.MinValue;
                if (el.TryGetProperty("published_at", out var d) && d.ValueKind == JsonValueKind.String)
                    DateTimeOffset.TryParse(d.GetString(), out date);

                items.Add(new NewsItem(
                    Title:   el.TryGetProperty("name",     out var n) ? n.GetString() ?? "" : "",
                    Body:    el.TryGetProperty("body",     out var b) ? b.GetString() ?? "" : "",
                    Version: NormalizeTag(tag) ?? "",
                    Date:    date,
                    Url:     el.TryGetProperty("html_url", out var u) ? u.GetString() : null
                ));
                if (items.Count >= 10) break;
            }
            return items.ToArray();
        }
        catch { return Array.Empty<NewsItem>(); }
    }

    // ── Private helpers — release resolution ──────────────────────────────────

    private static string? NormalizeTag(string? tag)
    {
        if (string.IsNullOrWhiteSpace(tag)) return null;
        tag = tag.Trim();
        return tag.StartsWith('v') && tag.Length > 1 && char.IsDigit(tag[1])
            ? tag[1..]
            : tag;
    }

    /// Resolve the latest AP-flavoured wwrando release (tags starting with "ap").
    /// Returns (version, windowsZipUrl, zipAssetName). Falls back to pinned when
    /// the API is unreachable.
    private async Task<(string Version, string? ZipUrl, string? ZipName)>
        ResolveLatestWwrandoAsync(CancellationToken ct)
    {
        try
        {
            string json = await _http.GetStringAsync(GH_WWRANDO_RELEASES_URL, ct);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.ValueKind != JsonValueKind.Array)
                goto fallback;

            foreach (var rel in doc.RootElement.EnumerateArray())
            {
                string? tag = rel.TryGetProperty("tag_name", out var t) ? t.GetString() : null;
                // Only consider AP-flavoured releases.
                if (tag == null || !tag.StartsWith("ap", StringComparison.OrdinalIgnoreCase))
                    continue;

                if (!rel.TryGetProperty("assets", out var assets) ||
                    assets.ValueKind != JsonValueKind.Array)
                    continue;

                foreach (var a in assets.EnumerateArray())
                {
                    string? name = a.TryGetProperty("name", out var n) ? n.GetString() : null;
                    string? url  = a.TryGetProperty("browser_download_url", out var u) ? u.GetString() : null;
                    if (name == null || url == null) continue;

                    string lower = name.ToLowerInvariant();
                    if (!lower.EndsWith(".zip")) continue;
                    if (lower.Contains("linux") || lower.Contains("macos") || lower.Contains("mac") ||
                        lower.Contains("qt5"))   continue;  // skip non-Windows; also prefer non-qt5 build
                    if (lower.Contains("windows") || lower.Contains("win"))
                        return (tag, url, name);
                }

                // Last resort: any non-linux/mac zip in this AP release.
                foreach (var a in assets.EnumerateArray())
                {
                    string? name = a.TryGetProperty("name", out var n) ? n.GetString() : null;
                    string? url  = a.TryGetProperty("browser_download_url", out var u) ? u.GetString() : null;
                    if (name == null || url == null) continue;
                    string lower = name.ToLowerInvariant();
                    if (!lower.EndsWith(".zip")) continue;
                    if (lower.Contains("linux") || lower.Contains("macos") || lower.Contains("mac")) continue;
                    return (tag, url, name);
                }
            }
        }
        catch (OperationCanceledException) { throw; }
        catch { /* API unreachable → pinned fallback */ }

        fallback:
        return (FallbackWwrandoVersion, FallbackWwrandoZipUrl, FallbackWwrandoZipName);
    }

    // ── Private helpers — wwrando exe resolution ──────────────────────────────

    /// Find the wwrando AP exe in our install folder. Prefers the first exe whose
    /// name contains "wwrando" or "rando" (excluding installers/helpers). Falls
    /// back to the single non-helper exe if exactly one exists.
    private string? ResolveWwrandoExe()
    {
        try
        {
            if (!Directory.Exists(WwrandoDirectory)) return null;

            string[] exes = Directory.EnumerateFiles(
                WwrandoDirectory, "*.exe", SearchOption.TopDirectoryOnly).ToArray();

            // Prefer a plausibly-named app exe.
            foreach (string exe in exes)
            {
                string name = Path.GetFileNameWithoutExtension(exe).ToLowerInvariant();
                if (IsHelperExe(name)) continue;
                if (name.Contains("wwrando") || name.Contains("rando") ||
                    name.Contains("wind")    || name.Contains("waker"))
                    return exe;
            }
            // Single non-helper exe.
            string[] candidates = exes
                .Where(e => !IsHelperExe(Path.GetFileNameWithoutExtension(e).ToLowerInvariant()))
                .ToArray();
            return candidates.Length == 1 ? candidates[0] : null;
        }
        catch { return null; }
    }

    private static bool IsHelperExe(string nameLowerNoExt)
        => nameLowerNoExt.Contains("unins")    ||
           nameLowerNoExt.Contains("setup")    ||
           nameLowerNoExt.Contains("crash")    ||
           nameLowerNoExt.Contains("vcredist") ||
           nameLowerNoExt.Contains("dxsetup");

