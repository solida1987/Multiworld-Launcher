    // ── Private helpers — ISO validation ─────────────────────────────────────

    /// Validate a GCN ISO: reads first 6 bytes and checks for TWW NA disc ID
    /// "GZLE99", and checks file size. Returns null if valid, else error message.
    private static string? ValidateIso(string path)
    {
        if (string.IsNullOrWhiteSpace(path))
            return "No file selected.";

        string ext = Path.GetExtension(path).ToLowerInvariant();
        if (ext != ".iso" && ext != ".gcm")
            return "The file must be a GameCube ISO (.iso or .gcm).";

        FileInfo fi;
        try { fi = new FileInfo(path); }
        catch { return "Could not access the file. Is it on a network drive or locked?"; }

        if (!fi.Exists)
            return "File does not exist: " + path;

        if (fi.Length < MinIsoBytes || fi.Length > MaxIsoBytes)
            return $"Unexpected file size ({fi.Length / (1024 * 1024)} MB). " +
                   "A Wind Waker ISO should be between 400 MB and 4 GB.";

        try
        {
            byte[] header = new byte[TwwDiscId.Length];
            using var f = File.OpenRead(path);
            int read = f.Read(header, 0, header.Length);
            if (read < TwwDiscId.Length)
                return "Could not read the disc header. The file may be corrupt.";

            for (int i = 0; i < TwwDiscId.Length; i++)
            {
                if (header[i] != TwwDiscId[i])
                    return $"This does not appear to be a North American Wind Waker disc " +
                           $"(disc ID is \"{System.Text.Encoding.ASCII.GetString(header)}\" — " +
                           $"expected \"GZLE99\"). Only the North American GameCube release is " +
                           $"supported. Japanese is GZLJ01, European is GZLP01.";
            }
        }
        catch (Exception ex)
        {
            return "Could not read the ISO header: " + ex.Message;
        }

        return null; // valid
    }

    // ── Private helpers — patched ISO resolution ──────────────────────────────

    /// Find the most recently written .iso or .gcm in the patched ISO folder.
    private string? FindMostRecentPatchedIso()
    {
        try
        {
            if (!Directory.Exists(PatchedIsoDirectory)) return null;
            return Directory
                .EnumerateFiles(PatchedIsoDirectory, "*.iso", SearchOption.TopDirectoryOnly)
                .Concat(Directory.EnumerateFiles(PatchedIsoDirectory, "*.gcm", SearchOption.TopDirectoryOnly))
                .OrderByDescending(f => File.GetLastWriteTimeUtc(f))
                .FirstOrDefault();
        }
        catch { return null; }
    }

    // ── Private helpers — download + extract wwrando ──────────────────────────

    private async Task DownloadAndExtractWwrandoAsync(
        string zipUrl,
        string zipName,
        string version,
        IProgress<(int Pct, string Msg)> progress,
        CancellationToken ct)
    {
        string tempZip = Path.Combine(Path.GetTempPath(),
            $"wwrando-{version}-{Guid.NewGuid():N}.zip");
        try
        {
            progress.Report((5, $"Downloading wwrando {version}..."));
            using var response = await _http.GetAsync(
                zipUrl, HttpCompletionOption.ResponseHeadersRead, ct);
            response.EnsureSuccessStatusCode();

            long total      = response.Content.Headers.ContentLength ?? -1;
            long downloaded = 0;

            await using (var src = await response.Content.ReadAsStreamAsync(ct))
            await using (var dst = File.Create(tempZip))
            {
                var buf = new byte[81920];
                int bytesRead;
                while ((bytesRead = await src.ReadAsync(buf, ct)) > 0)
                {
                    await dst.WriteAsync(buf.AsMemory(0, bytesRead), ct);
                    downloaded += bytesRead;
                    if (total > 0)
                    {
                        int pct = (int)(5 + 70 * downloaded / total);
                        progress.Report((pct, $"Downloading wwrando... {downloaded / 1_000_000} MB"));
                    }
                }
                await dst.FlushAsync(ct);
            }

            progress.Report((80, "Extracting wwrando..."));
            Directory.CreateDirectory(WwrandoDirectory);
            System.IO.Compression.ZipFile.ExtractToDirectory(tempZip, WwrandoDirectory, overwriteFiles: true);

            // Flatten a single top-level subfolder if the exe is not yet in the root.
            if (ResolveWwrandoExe() == null)
            {
                string[] subdirs = Directory.GetDirectories(WwrandoDirectory);
                if (subdirs.Length == 1)
                {
                    string sub = subdirs[0];
                    foreach (string fileSrc in Directory.EnumerateFiles(
                        sub, "*", SearchOption.AllDirectories))
                    {
                        string rel     = Path.GetRelativePath(sub, fileSrc);
                        string fileDst = Path.Combine(WwrandoDirectory, rel);
                        Directory.CreateDirectory(Path.GetDirectoryName(fileDst)!);
                        File.Move(fileSrc, fileDst, overwrite: true);
                    }
                    Directory.Delete(sub, recursive: true);
                }
            }

            progress.Report((95, "Finalizing..."));
        }
        finally
        {
            try { if (File.Exists(tempZip)) File.Delete(tempZip); } catch { }
        }
    }

