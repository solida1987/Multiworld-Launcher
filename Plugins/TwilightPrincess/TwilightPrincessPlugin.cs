    // ── Lifecycle — CheckForUpdate ────────────────────────────────────────────

    public async Task CheckForUpdateAsync(CancellationToken ct = default)
    {
        // "InstalledVersion" tracks the apworld release the user has noted.
        try
        {
            InstalledVersion = File.Exists(VersionFilePath)
                ? (await File.ReadAllTextAsync(VersionFilePath, ct)).Trim()
                : (IsRandoConfigured() ? "configured" : null);
        }
        catch { InstalledVersion = null; }

        try
        {
            // CDN HEAD redirect — no REST API quota consumed.
            AvailableVersion = GitHubHelper.NormalizeTag(
                await GitHubHelper.FetchLatestTagAsync(TpApworldOwner, TpApworldRepo, ct));
        }
        catch { AvailableVersion = null; }
    }

    // ── Lifecycle — InstallOrUpdate ───────────────────────────────────────────

    public async Task InstallOrUpdateAsync(
        IProgress<(int Pct, string Msg)> progress,
        CancellationToken ct = default)
    {
        await Task.CompletedTask;

        // Nothing to auto-download. Guide the user through prerequisites.
        progress.Report((10, "Checking prerequisites..."));

        var s = LoadSettings();
        var missing = new List<string>();

        if (string.IsNullOrWhiteSpace(s.RandoExePath) || !File.Exists(s.RandoExePath))
            missing.Add("TP Randomizer (download from tprandomizer.com)");

        string? dolphin = ResolveDolphinExe(s);
        if (dolphin == null)
            missing.Add("Dolphin Emulator (download from dolphin-emu.org)");

        string? apLauncher = FindApLauncherExe();
        if (apLauncher == null)
            missing.Add("Archipelago Launcher (install from archipelago.gg)");

        if (!string.IsNullOrWhiteSpace(s.VanillaIsoPath) && File.Exists(s.VanillaIsoPath))
        {
            string? isoErr = ValidateIso(s.VanillaIsoPath);
            if (isoErr != null)
                missing.Add("Valid North American Twilight Princess ISO (see Settings)");
        }
        else
        {
            missing.Add("Twilight Princess ISO — North American GCN or Wii (see Settings)");
        }

        progress.Report((50, "Checking apworld releases..."));

        string? latestApworld = null;
        try
        {
            string json = await _http.GetStringAsync(TpApworldReleasesUrl, ct);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.ValueKind == JsonValueKind.Array)
            {
                var first = doc.RootElement.EnumerateArray().FirstOrDefault();
                if (first.ValueKind == JsonValueKind.Object &&
                    first.TryGetProperty("tag_name", out var t))
                    latestApworld = NormalizeTag(t.GetString());
            }
        }
        catch { /* network unavailable — skip */ }

        if (latestApworld != null)
            AvailableVersion = latestApworld;

        Directory.CreateDirectory(GameDirectory);

        if (missing.Count > 0)
        {
            string missingList = string.Join("\n  • ", missing);
            progress.Report((100,
                $"Setup incomplete. Missing:\n  • {missingList}\n\n" +
                "STEPS:\n" +
                "  1. Download and install the TP Randomizer from tprandomizer.com.\n" +
                "     Set its path in the Settings tab.\n" +
                "  2. Install Dolphin from dolphin-emu.org.\n" +
                "  3. Install Archipelago from archipelago.gg (provides the TP Client).\n" +
                "  4. Download the Twilight Princess apworld (.apworld file) from:\n" +
                "     " + TpApworldRepoUrl + "/releases\n" +
                "     Copy the .apworld file into your Archipelago worlds/ folder.\n" +
                "  5. Set your North American Twilight Princess ISO in the Settings tab.\n\n" +
                "For full setup instructions, see:\n" + SetupGuideUrl));
            return;
        }

        progress.Report((100,
            "All prerequisites found.\n\n" +
            "HOW TO PLAY:\n" +
            "  1. Generate your multiworld and download your .aptp seed file.\n" +
            "  2. Open the TP Randomizer (Settings tab > Open Randomizer).\n" +
            "     Load your vanilla ISO and the .aptp file, then generate the patched data.\n" +
            "  3. Place RandomizerAP.US.gci and the REL loader in Dolphin's GCN save folder.\n" +
            "  4. Click Play — the launcher starts the Twilight Princess Client (via the\n" +
            "     Archipelago Launcher) and then Dolphin with your vanilla ISO.\n\n" +
            "Refer to the full setup guide for patching details:\n" + SetupGuideUrl));
    }

    // ── Lifecycle — Verify ────────────────────────────────────────────────────

    public async Task<bool> VerifyInstallAsync(CancellationToken ct = default)
    {
        await Task.CompletedTask;
        var s = LoadSettings();
        return IsRandoConfigured(s)
            && ResolveDolphinExe(s) != null
            && FindApLauncherExe() != null
            && !string.IsNullOrWhiteSpace(s.VanillaIsoPath)
            && File.Exists(s.VanillaIsoPath)
            && ValidateIso(s.VanillaIsoPath) == null;
    }

    // ── ConnectsItself / SupportsStandalone ───────────────────────────────────

    /// The TP Client (via AP Launcher) owns the AP slot — the launcher must not
    /// hold a competing ApClient connection while the game is running.
    public bool ConnectsItself     => true;

    /// No standalone (non-AP) play through this integration.
    public bool SupportsStandalone => false;

    // ── Lifecycle — Launch ────────────────────────────────────────────────────

    public Task LaunchAsync(ApSession session, CancellationToken ct = default)
    {
        var s = LoadSettings();

        // Guard: ISO must be configured.
        if (string.IsNullOrWhiteSpace(s.VanillaIsoPath) || !File.Exists(s.VanillaIsoPath))
        {
            MessageBox.Show(
                "No Twilight Princess ISO configured. Please set your North American " +
                "GCN or Wii ISO path in the Settings tab first.",
                "ISO Not Configured",
                MessageBoxButton.OK,
                MessageBoxImage.Warning);
            return Task.CompletedTask;
        }

        // Start the TP Client via ArchipelagoLauncher.exe.
        StartTpClient(session);

        // Start Dolphin with the vanilla ISO.
        StartDolphin(s.VanillaIsoPath);

        return Task.CompletedTask;
    }

    public Task StopAsync()
    {
        try { _tpClientProcess?.Kill(entireProcessTree: true); } catch { }
        try { _dolphinProcess?.Kill(entireProcessTree: true); } catch { }
        IsRunning        = false;
        _tpClientProcess = null;
        _dolphinProcess  = null;
        return Task.CompletedTask;
    }

