    // ── Private helpers — ISO validation ─────────────────────────────────────

    /// Returns null if the ISO is acceptable; otherwise a human-readable reason.
    private static string? ValidateIso(string path)
    {
        if (string.IsNullOrWhiteSpace(path))
            return "No file selected.";

        string ext = Path.GetExtension(path).ToLowerInvariant();
        if (ext != ".iso" && ext != ".gcm")
            return "The file must be a GameCube/Wii ISO (.iso or .gcm).";

        FileInfo fi;
        try { fi = new FileInfo(path); }
        catch { return "Could not access the file. Is it on a network drive or locked?"; }

        if (!fi.Exists)
            return "File does not exist: " + path;

        if (fi.Length < MinIsoBytes || fi.Length > MaxIsoBytes)
            return $"Unexpected file size ({fi.Length / (1024 * 1024)} MB). " +
                   "A Twilight Princess GCN ISO is ~1,400 MB; a Wii ISO is ~7,900 MB.";

        try
        {
            byte[] header = new byte[TpDiscIdPrefix.Length];
            using var f = File.OpenRead(path);
            int read = f.Read(header, 0, header.Length);
            if (read < TpDiscIdPrefix.Length)
                return "Could not read the disc header. The file may be corrupt.";

            for (int i = 0; i < TpDiscIdPrefix.Length; i++)
            {
                if (header[i] != TpDiscIdPrefix[i])
                {
                    string found = Encoding.ASCII.GetString(header);
                    return $"This does not appear to be a North American Twilight Princess " +
                           $"disc (found game code \"{found}\" — expected \"RZDE\"). " +
                           "Japanese is RZDJ01, PAL is RZDP01; only the North American " +
                           "release (RZDE01) is supported by the TP Randomizer.";
                }
            }
        }
        catch (Exception ex)
        {
            return "Could not read the ISO header: " + ex.Message;
        }

        return null; // valid
    }

    // ── Private helpers — settings sidecar ───────────────────────────────────

    private bool IsRandoConfigured(TpSettings? s = null)
    {
        s ??= LoadSettings();
        return !string.IsNullOrWhiteSpace(s.RandoExePath) && File.Exists(s.RandoExePath);
    }

    private static string? NormalizeTag(string? tag)
    {
        if (string.IsNullOrWhiteSpace(tag)) return null;
        tag = tag.Trim();
        return tag.StartsWith('v') && tag.Length > 1 && char.IsDigit(tag[1])
            ? tag[1..] : tag;
    }

