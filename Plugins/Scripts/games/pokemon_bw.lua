-- pokemon_bw.lua — game module for the Archipelago BizHawk connector.
--                  Pokémon Black and White (Nintendo DS, BizHawk melonDS core)
--
-- STATUS: location DETECTION + goal are REAL and SOURCE-DERIVED from the official
-- community AP world https://github.com/BlastSlimey/PokemonBWAP
-- (pokemon_bw/bizhawk_client.py + client/locations.py + client/goals.py +
-- generate/locations/*.py, apworld pokemon_bw.apworld v0.3.999, world_version
-- 0.3.200, minimum_ap_version 0.6.3). The 633-entry flag-location table and
-- 649-entry dex-location table below were GENERATED by importing the world's OWN
-- data tables (data/locations/ingame_items/*.py + data/locations/dexsanity.py)
-- and running its OWN lookup() id-assignment formulas with Python (NOT
-- hand-copied), so the ap_id → flag/dex mapping is byte-for-byte what
-- PokemonBWClient.game_watcher uses. Loads crash-free on any cartridge;
-- self-disables on a non-AP ROM (validates the NDS game-title header, exactly
-- like client.validate_rom).
--
-- WHY A GENERATED TABLE (not an algorithmic loc_id→flag formula): the world's
-- lookup() builds ids as flag_id*100 + domain + suffix for the `other` (300000)
-- and `tm_hm` (500000) domains, so a two-digit "#nn" suffix pushes some ids OUT
-- of their nominal 100000-band (e.g. flag 1439 → id 443900, which naively looks
-- like a 400000 "badge" id). 42 ids collide bands this way — a band-based reverse
-- formula is provably wrong for them, so the exact generated table is authoritative.
--
-- MEMORY MODEL (BizHawk melonDS NDS domains)
-- ───────────────────────────────────────────
--   PokemonBWClient is a BizHawkClient. EVERY save-data read AND the ROM-identity
--   header read in bizhawk_client.py use the "Main RAM" domain (ram_read_write_
--   domain) — NOT "ARM9 System Bus" (that is what Pokémon Platinum uses; BW is
--   different). This module follows the apworld and uses "Main RAM" for all reads,
--   so the discovered pointer/flag values stay consistent with the apworld.
--
--   Save-data discovery (client.early_setup):
--     save_data_address = u24 LE @ 0x000024 ("Main RAM")  (a 3-byte read; ≈
--       0x21B310 in vanilla White). All save offsets below are relative to it.
--   In-game gate (client.game_watcher):
--     u8 @ 0x000034 ("Main RAM"); 0 = not in a loaded save → skip this poll.
--   ROM identity (client.validate_rom): 18 bytes @ 0x3FFA80 ("Main RAM") must
--     equal  b'POKEMON B\0\0\0IRBO01' (Black)  or  b'POKEMON W\0\0\0IRAO01'
--     (White). The AP-patched ROM keeps the original gamecode, so this is the
--     same signature on a patched cartridge — it disables only on a non-BW ROM.
--
--   Flags block  @ save + 0x20C38, size ceil(2912/8)=364 bytes (bit array).
--   Dex block    @ save + 0x21EC4, size ceil(650/8)=82  bytes (caught-flags bits).
--   Vars block   @ save + 0x209BC (u16 LE array; var #v at +2*v) — goal var reads.
--
-- LOCATION MODEL (client check_flag_locations / check_dex_locations)
--   FLAG locations (overworld 100000 / hidden 200000 / other 300000 /
--     badge 400000 / tm_hm 500000 domains): set when
--       flags[flag_id // 8] & (1 << (flag_id % 8)) != 0.
--     Multiple ap_ids can share one flag_id (suffix "#1/#2" variants) — when the
--     flag sets, ALL wanted ap_ids on that flag report, exactly as the client's
--     missing_flag_loc_ids[flag] list does.
--   DEX locations (dexsanity 600000 domain): set when
--       dex[(dex_number-1) // 8] & (1 << ((dex_number-1) % 8)) != 0.
--     (client maps dex bit k → dex_number k+1, i.e. dex_number d → bit d-1.)
--     Only present when slot_data options.dexsanity != 0 (else the client never
--     reads the dex block — this module mirrors that and skips it).
--   Only ap_ids in the slot's server-location set are reported.
--
-- GOAL (client.goals.get_method, by slot_data options.goal — DEFAULT "ghetsis"):
--   ghetsis          → flag 0x1D3
--   champion         → flag 0x1D4
--   cynthia          → var 0xE4 >= 2
--   cobalion         → flag 649
--   tmhm_hunt        → flag 0x191
--   seven_sages_hunt → var 0xCC >= 6  AND flag 0x1D3
--   legendary_hunt   → flags 649,650,651,801,779,810 AND flag 0x1CE
--   pokemon_master   → ALL of the above combined
--   (When slot_data is unavailable the module defaults to "ghetsis", the world's
--    own Goal default = 0.)
--
-- ITEMS: items_handling = 0b111 (ctx.items_handling set in client.validate_rom) —
--   the AP SERVER drives ALL item delivery, including this slot's own and remote
--   items. The reference client WRITES received items into the running game's
--   save RAM and bumps the game's own received-item counter (a guarded multi-write
--   into the live save). That guarded-write path is the documented deferred piece
--   (receive_item below is a no-op) until it can be confirmed in-emulator, rather
--   than shipped unverified — a wrong save-RAM write would corrupt the save. Same
--   posture as ff1.lua / pokemon_platinum.lua / cvcotm.lua. Checks + goal flow
--   regardless; the launcher still replays the full item stream, this module just
--   does not consume it yet.
--
