-- ── State ─────────────────────────────────────────────────────────────────────
local log_fn            = nil
local mem               = {}
local rom_ok            = nil       -- AP ROM identity (nil=unknown,false=no,true=yes)
local rom_version       = nil       -- "black" / "white" once identified
local why_disabled      = "init() not run"
local server_locations  = nil       -- set of ap_ids the server expects (nil = all)
local reported          = {}        -- ap_id -> true once returned from poll()
local goal_reached      = false
local goal_name         = "ghetsis" -- from slot_data; default = world's Goal default
local dexsanity_on      = true      -- slot_data options.dexsanity != 0
local n_flag, n_dex     = 0, 0

-- Powers of two, indices 0..8 (the [8]=256 entry is needed by the modulo bit
-- test below: testing bit 7 uses POW2[(id%8)+1] = POW2[8]).
local POW2 = { [0]=1, 2, 4, 8, 16, 32, 64, 128, 256 }

local function log(msg)
  if log_fn then pcall(log_fn, "[pokemon_bw] " .. tostring(msg)) end
end

-- ── Memory API (resolved at init; 2-arg domain form + current-domain fallback) ─
local function resolve_memory_api()
  if not memory then return false end
  mem.read_u8  = memory.read_u8     or memory.readbyte
  mem.read_u16 = memory.read_u16_le or memory.readword
  mem.read_u24 = memory.read_u24_le
  mem.read_u32 = memory.read_u32_le or memory.readdword
  mem.read_rng = memory.read_bytes_as_array or memory.readbyterange
  return mem.read_u8 ~= nil
end

local function rd(fn, addr, domain)
  if not fn then return nil end
  local ok, v = pcall(fn, addr, domain)
  if ok and type(v) == "number" then return v end
  ok, v = pcall(fn, addr)                       -- older API: current domain
  if ok and type(v) == "number" then return v end
  return nil
end

local function read_u8(a, d)  return rd(mem.read_u8,  a, d) end
local function read_u16(a, d) return rd(mem.read_u16, a, d) end

-- Read `len` bytes → 1-based array (or nil). Bulk API first (both 0- and 1-based
-- variants normalized), per-byte fallback.
local function read_range(addr, len, domain)
  if mem.read_rng then
    local ok, t = pcall(mem.read_rng, addr, len, domain)
    if ok and type(t) == "table" then
      if t[0] ~= nil then
        local out = {}
        for i = 0, len - 1 do out[i + 1] = t[i] end
        return out
      end
      if t[1] ~= nil or len == 0 then return t end
    end
  end
  local out = {}
  for i = 0, len - 1 do
    local v = read_u8(addr + i, domain)
    if not v then return nil end
    out[i + 1] = v
  end
  return out
end

-- u24 LE read with fallback to three u8 reads (read_u24_le may be absent).
local function read_u24(addr, domain)
  local v = rd(mem.read_u24, addr, domain)
  if v ~= nil then return v end
  local b = read_range(addr, 3, domain)
  if not b then return nil end
  return b[1] + b[2] * 256 + b[3] * 65536
end

-- ── ROM identity: mirror client.validate_rom (18-byte NDS header @ Main RAM) ──
local function header_matches(bytes, sig)
  if #bytes < #sig then return false end
  for i = 1, #sig do
    if bytes[i] ~= string.byte(sig, i) then return false end
  end
  return true
end

local function rom_is_ap()
  if rom_ok ~= nil then return rom_ok end
  local b = read_range(HEADER_ADDR, 18, RAM)
  if not b then return false end                 -- RAM not ready; retry
  if header_matches(b, HEADER_BLACK) then
    rom_ok = true; rom_version = "black"
    log("AP ROM verified: Pokémon Black (IRBO)")
    return true
  end
  if header_matches(b, HEADER_WHITE) then
    rom_ok = true; rom_version = "white"
    log("AP ROM verified: Pokémon White (IRAO)")
    return true
  end
  -- A short/zero header during boot reads as not-yet-ready, not a wrong ROM —
  -- keep rom_ok nil so we retry until the header is populated.
  local nonzero = false
  for i = 1, #b do if b[i] and b[i] ~= 0 then nonzero = true; break end end
  if not nonzero then return false end
  rom_ok = false
  why_disabled = "loaded ROM is not a Pokémon Black/White (US) cartridge — " ..
                 "checks/goal DISABLED"
  log(why_disabled)
  return false
end

-- ── Multiworld context ────────────────────────────────────────────────────────
local function load_locations(ids)
  if type(ids) ~= "table" then return end
  server_locations = {}
  local n = 0
  for _, id in ipairs(ids) do
    local v = tonumber(id)
    if v then server_locations[v] = true; n = n + 1 end
  end
  log("server location set: " .. n .. " ids")
end

local function wanted(ap_id)
  if server_locations == nil then return true end
  return server_locations[ap_id] == true
end

-- Apply slot_data: goal name + dexsanity inclusion (client.early_setup /
-- goals.get_method). Tolerant of the various shapes ctx.config may carry.
local function apply_slot_data(cfg)
  local sd = cfg and (cfg.slot_data or cfg.slotdata)
  if type(sd) ~= "table" then return end
  local opts = sd.options or sd
  if type(opts) == "table" then
    if type(opts.goal) == "string" then goal_name = opts.goal end
    if opts.dexsanity ~= nil then
      local d = tonumber(opts.dexsanity)
      dexsanity_on = not (d == 0)
    end
  end
end

-- ── Bit test in a 1-based byte array ──────────────────────────────────────────
-- flag id // 8 → byte (1-based), id % 8 → bit. (client.get_flag.)
local function bit_set(arr, id)
  local byte = math.floor(id / 8) + 1
  local v = arr[byte]
  if not v then return false end
  return (v % POW2[(id % 8) + 1]) >= POW2[id % 8]
end

-- ── Module contract ───────────────────────────────────────────────────────────
function M.init(ctx)
  if ctx and type(ctx.log) == "function" then log_fn = ctx.log end
  if not ADDRESSES_VERIFIED then
    log("address map not verified — module idle"); return
  end
  if not resolve_memory_api() then
    why_disabled = "BizHawk memory API unavailable"; log(why_disabled)
    ADDRESSES_VERIFIED = false; return
  end
  local cfg = (ctx and ctx.config) or {}
  load_locations(cfg.locations)
  apply_slot_data(cfg)
  for _ in pairs(FLAG_LOC) do n_flag = n_flag + 1 end
  for _ in pairs(DEX_LOC)  do n_dex  = n_dex  + 1 end
  log(string.format("ready: %d flag + %d dex location checks, goal '%s', dexsanity %s",
                    n_flag, n_dex, goal_name, tostring(dexsanity_on)))
end

-- Read the flags block (save-relative). Returns 1-based byte array or nil.
local function read_flags(save_addr)
  return read_range(save_addr + FLAGS_OFF, FLAGS_BYTES, RAM)
end
local function read_dex(save_addr)
  return read_range(save_addr + DEX_OFF, DEX_BYTES, RAM)
end
local function read_var(save_addr, var_idx)
  return read_u16(save_addr + VAR_OFF + 2 * var_idx, RAM)
end

-- Goal evaluation against the freshly-read flags block + on-demand var reads
-- (client.goals.*). flags is the 1-based byte array; save_addr for var reads.
local function goal_met(flags, save_addr)
  local function fset(id) return bit_set(flags, id) end
  local function vge(var, val)
    local v = read_var(save_addr, var)
    return v ~= nil and v >= val
  end
  local function ghetsis()        return fset(GOAL_FLAG_GHETSIS) end
  local function champion()       return fset(GOAL_FLAG_CHAMPION) end
  local function cynthia()        return vge(GOAL_VAR_CYNTHIA, 2) end
  local function cobalion()       return fset(GOAL_FLAG_COBALION) end
  local function tmhm()           return fset(GOAL_FLAG_TMHM) end
  local function seven_sages()    return vge(GOAL_VAR_SAGES, 6) and ghetsis() end
  local function legendary()
    for _, f in ipairs(LEGENDARY_FLAGS) do if not fset(f) then return false end end
    return true
  end

  if     goal_name == "ghetsis"          then return ghetsis()
  elseif goal_name == "champion"         then return champion()
  elseif goal_name == "cynthia"          then return cynthia()
  elseif goal_name == "cobalion"         then return cobalion()
  elseif goal_name == "tmhm_hunt"        then return tmhm()
  elseif goal_name == "seven_sages_hunt" then return seven_sages()
  elseif goal_name == "legendary_hunt"   then return legendary()
  elseif goal_name == "pokemon_master"   then
    return ghetsis() and champion() and cynthia() and cobalion()
       and tmhm() and seven_sages() and legendary()
  else
    return false   -- unknown goal → never auto-complete (client.error)
  end
end

function M.poll()
  local out = {}
  if not ADDRESSES_VERIFIED then return out end
  if not rom_is_ap() then return out end

  -- In-game gate (client.game_watcher: ingame_state byte must be nonzero).
  local state = read_u8(INGAME_STATE, RAM)
  if not state or state == 0 then return out end

  -- Discover the save-data base (client.early_setup).
  local save_addr = read_u24(DATA_ADDR_PTR, RAM)
  if not save_addr or save_addr < SAVE_ADDR_MIN then return out end

  -- Bulk flag read (client.check_flag_locations).
  local flags = read_flags(save_addr)
  if not flags then return out end

  for raw, flag_id in pairs(FLAG_LOC) do
    if not reported[raw] and wanted(raw) and bit_set(flags, flag_id) then
      reported[raw] = true
      out[#out + 1] = raw
    end
  end

  -- Dex reads only when dexsanity is enabled (client.check_dex_locations skips
  -- the read entirely when client.dexsanity_included is false).
  if dexsanity_on then
    local dex = read_dex(save_addr)
    if dex then
      for raw, dex_number in pairs(DEX_LOC) do
        if not reported[raw] and wanted(raw) and bit_set(dex, dex_number - 1) then
          reported[raw] = true
          out[#out + 1] = raw
        end
      end
    end
  end

  -- Goal (client.goal_checking_method for the slot's goal option).
  if not goal_reached and goal_met(flags, save_addr) then
    goal_reached = true
    log("goal '" .. goal_name .. "' satisfied — goal complete")
  end

  return out
end

function M.is_goal_complete()
  return goal_reached
end

