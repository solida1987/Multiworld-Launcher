-- ── State ─────────────────────────────────────────────────────────────────────
local reported         = {}     -- ap_id -> true once returned from poll()
local server_locations = nil    -- set of ap_ids the server expects (nil = all)
local rom_ok           = nil     -- cached AP-name result
local goal_reached     = false
local mem              = {}
local log_fn           = nil

-- ── Logging ───────────────────────────────────────────────────────────────────
local function log(msg)
  if log_fn then pcall(log_fn, "[wario_land_4] " .. tostring(msg)) end
end

-- ── Memory API (resolved at init; 2-arg domain form + current-domain fallback) ─
local function resolve_memory_api()
  if not memory then return false end
  mem.read_u8  = memory.read_u8     or memory.readbyte
  mem.read_u16 = memory.read_u16_le or memory.readword
  mem.read_u32 = memory.read_u32_le or memory.readdword
  return mem.read_u8 ~= nil
end

local function rd(fn, addr, domain)
  if not fn then return nil end
  local ok, v = pcall(fn, addr, domain)
  if ok and type(v) == "number" then return v end
  ok, v = pcall(fn, addr)            -- older API: current domain
  if ok and type(v) == "number" then return v end
  return nil
end

local function read_u8(addr, domain)  return rd(mem.read_u8,  addr, domain) end
local function read_u16(addr, domain) return rd(mem.read_u16, addr, domain) end
local function read_u32(addr, domain) return rd(mem.read_u32, addr, domain) end

-- ── Bit helpers (pure Lua, no bit library dependency) ─────────────────────────
local function bit_and(a, b)
  local res, bitval = 0, 1
  while a > 0 and b > 0 do
    if a % 2 == 1 and b % 2 == 1 then res = res + bitval end
    a = math.floor(a / 2); b = math.floor(b / 2); bitval = bitval * 2
  end
  return res
end

-- status_word >> 8 (logical shift on a non-negative 32-bit value).
local function shr8(v)
  return math.floor(v / 256)
end

-- ── ROM identity: the patched cartridge header reads "WARIOLANDAP{E,J}" ────────
local function rom_is_ap()
  if rom_ok ~= nil then return rom_ok end
  local chars = {}
  for i = 0, #AP_NAME_US - 1 do
    -- Read the 12-byte name through the System Bus (the ROM lives at 0x08000000
    -- on the bus); fall back to the "ROM" domain only if System Bus refuses.
    local b = read_u8(ROM_NAME_ADDR + i, SYSBUS)
    if b == nil then b = read_u8((ROM_NAME_ADDR - 0x08000000) + i, ROMDOM) end
    if b == nil then return false end          -- not readable yet; retry next poll
    chars[#chars + 1] = string.char(b)
  end
  local name = table.concat(chars)
  if name ~= AP_NAME_US and name ~= AP_NAME_JP then
    rom_ok = false
    log("non-AP/non-WL4 ROM (header '" .. name .. "') — detection idle")
    return false
  end
  rom_ok = true
  log("AP ROM verified (header '" .. name .. "')")
  return true
end

-- ── Multiworld context ────────────────────────────────────────────────────────
local function load_locations(ids)
  if type(ids) ~= "table" then return end
  server_locations = {}
  local n = 0
  for _, id in ipairs(ids) do
    local v = tonumber(id)
    if v then server_locations[v] = true; n = n + 1 end
  end
  log("server location set: " .. n .. " ids")
end

local function wanted(ap_id)
  if server_locations == nil then return true end
  return server_locations[ap_id] == true
end

-- ── Detection gate (handle_inventory): GlobalGameMode ∈ {SELECT, INGAME} ──────
local function in_gameplay()
  local mode = read_u16(GAME_MODE_ADDR, SYSBUS)
  return mode == GAMEMODE_SELECT or mode == GAMEMODE_INGAME
end

-- Cache the 36-entry inventory once per poll (status_word per passage/level).
local inv = {}     -- key = passage*6+level -> u32 status word

local function refresh_inventory()
  for slot = 0, PASSAGE_COUNT * LEVELS_PER_PASS - 1 do
    inv[slot] = read_u32(ITEM_STATUS_ADDR + slot * 4, SYSBUS)
  end
end

-- A location is checked when (status_word >> 8) & flag_value ~= 0
-- (get_collected_locations: collection = status_bits >> 8; if collection & bit).
local function location_checked(passage, level, flag_value)
  local status = inv[passage * LEVELS_PER_PASS + level]
  if status == nil then return false end
  return bit_and(shr8(status), flag_value) ~= 0
end

-- ── Module contract ───────────────────────────────────────────────────────────
function M.init(ctx)
  if ctx and type(ctx.log) == "function" then log_fn = ctx.log end
  if not resolve_memory_api() then
    log("BizHawk memory API unavailable — module idle")
    ADDRESSES_VERIFIED = false
    return
  end
  local cfg = (ctx and ctx.config) or {}
  load_locations(cfg.locations)
  local n = 0; for _ in pairs(LOC) do n = n + 1 end
  log("ready: " .. n .. " location flags (System Bus inventory scan)")
end

function M.poll()
  local new = {}
  if not ADDRESSES_VERIFIED then return new end
  if not rom_is_ap() then return new end
  if not in_gameplay() then return new end

  refresh_inventory()
  for ap_id, info in pairs(LOC) do
    if not reported[ap_id] and wanted(ap_id)
       and location_checked(info[1], info[2], info[3]) then
      reported[ap_id] = true
      new[#new + 1] = ap_id
    end
  end

  -- Goal: the GOLDEN/BOSS_LEVEL status word & DIVA_CLEAR (read RAW, not >>8).
  if not goal_reached then
    local status = inv[GOAL_PASSAGE * LEVELS_PER_PASS + GOAL_LEVEL]
    if status == nil then
      status = read_u32(ITEM_STATUS_ADDR + (GOAL_PASSAGE * LEVELS_PER_PASS + GOAL_LEVEL) * 4, SYSBUS)
    end
    if status and bit_and(status, GOAL_MASK) ~= 0 then
      goal_reached = true
      log("goal reached (Golden Diva defeated — DIVA_CLEAR set)")
    end
  end

  return new
end

function M.is_goal_complete()
  if not ADDRESSES_VERIFIED or not rom_is_ap() then return false end
  if goal_reached then return true end
  -- Re-read the goal word directly so a goal-only poll still answers correctly.
  local status = read_u32(
    ITEM_STATUS_ADDR + (GOAL_PASSAGE * LEVELS_PER_PASS + GOAL_LEVEL) * 4, SYSBUS)
  if status and bit_and(status, GOAL_MASK) ~= 0 then
    goal_reached = true
  end
  return goal_reached
end

-- Remote multiworld items: see the file header. items_handling = LOCAL_ITEMS
-- (0b001) means the PATCHED GAME grants its own locally-found items, so solo
-- play and check reporting work fully; applying REMOTE items is the client's
