-- STATUS: location DETECTION + goal are REAL and SOURCE-DERIVED from that world
-- (client.py = WL4Client(BizHawkClient) + locations.py + data.py + rom.py, main
-- branch). The 292-entry location table below was GENERATED by importing the
-- world's own locations.py and calling LocationData.to_ap_id() — not hand-copied,
-- so every id is exact. Loads crash-free on any ROM and self-disables on a
-- non-AP / non-WL4 cartridge (the 12-byte ROM name guard).
--
-- MEMORY MODEL (BizHawk GBA "System Bus" domain — exactly what client.py reads)
-- ────────────────────────────────────────────────────────────────────────────
--   WL4Client reads everything through the GBA System Bus with ABSOLUTE
--   addresses (resolved from the world's basepatch.sym), not EWRAM-relative
--   offsets. We mirror that one-to-one:
--     System Bus 0x080000A0, 12  -> cartridge internal name (ROM identity)
--     System Bus 0x03000C3A  u16 -> GlobalGameMode (main game mode)
--     System Bus 0x03000A68      -> W4ItemStatus: the inventory array,
--                                   len(Passage)=6 * 6 levels * u32 = 144 bytes.
--   INVENTORY LAYOUT (handle_inventory): the array is 36 little-endian u32, one
--   per (passage, level): status_word = u32 @ W4ItemStatus + (passage*6+level)*4.
--   The per-level "collection" bitfield is status_word >> 8. A location is
--   CHECKED when  (status_word >> 8) & location.flag  is non-zero, where
--   location.flag is the raw ItemFlag value (JEWEL_NE=1<<0 … DIAMOND_11=1<<18,
--   CD=1<<4, KEYZER=1<<5). This is get_collected_locations() byte-for-byte.
--
-- DETECTION GATE (handle_inventory): GlobalGameMode ∈ {SELECT=1, INGAME=2}.
--   On the title cutscene (mode 0) the inventory is not yet meaningful, so the
--   client never scans it there — neither do we.
--
-- GOAL (handle_goal): u32 @ W4ItemStatus + 4*(6*GOLDEN + BOSS_LEVEL)
--   = W4ItemStatus + 4*(6*5 + 4) = W4ItemStatus + 0x88, masked with
--   ItemFlag.DIVA_CLEAR (1<<4 = 0x10): the Golden Diva defeated flag. (This
--   high word is read RAW, NOT >>8 — the client masks the raw status word.)
--   The flag only sets after the final boss, so it cannot false-positive before
--   then; we additionally require the AP ROM identity.
--
-- ITEM DELIVERY (items_handling = LOCAL_ITEMS = 0b001)
-- ──────────────────────────────────────────────────────────────────────────
--   WL4 is a SELF-GRANTING ROM: the patch makes the game award its own locally
--   found items, and items_handling=0b001 means the server sends only OTHER
--   players' items to us. A SOLO seed therefore plays fully and every check is
--   reported with NO item writes at all. Delivering REMOTE multiworld items is
--   the client's guarded System-Bus mailbox handshake (handle_received_items):
--   it writes IncomingItemID (0x03006280) + an encoded sender string into
--   IncomingItemSender (0x03006282) + sets MultiworldState (0x03006281) to
--   ITEM_QUEUED, all guarded on the game mode AND MultiworldState==IDLE, then
--   the game's ASM consumes it and bumps received_item_count (W4ItemStatus+14).
--   That write path needs in-emulator verification (a wrong System-Bus write
--   into the live game corrupts state), and the sender-string encoding depends
--   on per-seed player names the module is not handed, so it is intentionally
--   left as a documented NO-OP rather than shipped unverified. Checks + goal
--   flow fully regardless. See receive_item() at the bottom.
--
-- MODULE CONTRACT (called by bizhawk_ap_connector.lua)
--   M.init(ctx) / M.poll() -> {ids} / M.is_goal_complete() -> bool /
--   M.receive_item(item_id, meta)
-- ═══════════════════════════════════════════════════════════════════════════════

