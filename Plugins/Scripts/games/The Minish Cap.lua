--
-- MEMORY MODEL (BizHawk GBA domains — exactly the domains client.py reads)
-- ────────────────────────────────────────────────────────────────────────
--   "ROM"   cartridge: the 8-byte game identifier "GBAZELDA" @0xA0 (validate_rom),
--           the AP-patched player-name marker @0x600 (16 bytes; zero on a vanilla
--           cartridge — this is what distinguishes an AP ROM from a stock dump).
--   "IWRAM" internal RAM: game_task @0x1002, task_substate @0x1004 (gameplay gate
--           + goal), room/area id @0x0BF4 (u16 LE).
--   "EWRAM" work RAM: every location flag (per-location `ram_addr` byte), the
--           received-item index @0x2A44 (u16 BE), the item-delivery mailbox
--           @0x3FF10 + the "player safe" guard byte @0x2A4A, and the goal
--           trigger (game_task transitions to 0x04 on Vaati's defeat).
--
-- DETECTION (handle_location_sending, client.py line ~285)
--   For each server location with a readable `ram_addr = (addr, mask)`:
--     checked  when  (EWRAM[addr] | mask) == EWRAM[addr]      (ALL mask bits set)
--   This is the exact client predicate `loc_ram[0] | mask == loc_ram[0]` (Python
--   `|` binds tighter than `==`). Gated to the slot's server location set and to
--   the in-gameplay state so the title/file screens never report a check.
--
-- GOAL (game_watcher line ~236): game_task == 0x04. Vaati's defeat triggers a
--   cutscene that sets game_task to 0x04 — the client sends CLIENT_GOAL on that
--   transition. (Goal=Pedestal seeds patch the stained-glass scene to jump to the
--   same credits path, so this single trigger covers both goal options.)
--
-- ITEM DELIVERY (items_handling = 0b101: remote items + starting inventory)
-- ──────────────────────────────────────────────────────────────────────────
--   The Minish Cap is NOT a self-granting ROM for remote items. The client hands
--   the patched game each received item by writing the 2-byte AP item id
--   (pid = id>>8, sid = id&0xFF) into the EWRAM mailbox @0x3FF10, GUARDED so it
--   only writes while the mailbox reads [0,0] AND the ROM's "player safe" byte
--   @0x2A4A == 1 (set by the game when it is safe to receive). On a confirmed
--   write the game's ASM consumes the mailbox (zeroes it) and the client advances
--   the received-item counter (u16 BE @0x2A44). We replicate that exact guarded
--   handshake: one item per poll, indexed by the game's own received counter, and
--   EVERY write is gated behind the AP-ROM identity AND the in-gameplay state, so
--   the module can never write into a foreign or unpatched cartridge.
--   (remote_items=true upgrades the server to items_handling 0b111 client-side;
--   that path only changes WHICH items the server sends, not how we deliver them.)
--
-- NOT COVERED (documented, gated out so they are never falsely reported)
--   • 15 Goron Merchant slots (ids 6029034-6029048) and 100 Kinstone Fusion
--     locations (ids 6029601-6029700) have `ram_addr = None` in the source — the
--     client resolves them through its bit-counting `handle_special_sending`
--     (EWRAM 0x2CA3) and its event/data-store path, NOT a readable flag. Those
--     intricate stateful paths are deferred until they can be confirmed in-
--     emulator; their ids are filtered so this module never claims them.
--
