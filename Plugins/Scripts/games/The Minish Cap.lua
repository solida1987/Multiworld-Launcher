-- ── State ─────────────────────────────────────────────────────────────────────
local reported         = {}     -- ap_id -> true once returned from poll()
local server_locations = nil    -- set of ap_ids the server expects (nil = all)
local rom_ok           = nil     -- cached AP-ROM result
local goal_reached     = false
local mem              = {}
local log_fn           = nil

-- Item stream: raw_items[absolute AP index] = { id=, player=, flags=, location= }.
-- Delivery indexes it by the game's own received-item counter (EWRAM 0x2A44).
local raw_items        = {}
local legacy_next      = 0
local delivered_log    = 0

-- ── Logging ───────────────────────────────────────────────────────────────────
local function log(msg)
  if log_fn then pcall(log_fn, "[tmc] " .. tostring(msg)) end
end

-- ── Memory API (resolved at init; 2-arg domain form + current-domain fallback) ─
local function resolve_memory_api()
  if not memory then return false end
  mem.read_u8  = memory.read_u8  or memory.readbyte
  mem.write_u8 = memory.write_u8 or memory.writebyte
  return mem.read_u8 ~= nil
end

local function read_u8(addr, domain)
  if not mem.read_u8 then return nil end
  local ok, v = pcall(mem.read_u8, addr, domain)
  if ok and type(v) == "number" then return v end
  ok, v = pcall(mem.read_u8, addr)            -- older API: current domain
  if ok and type(v) == "number" then return v end
  return nil
end

local function write_u8(addr, value, domain)
  if not mem.write_u8 then return false end
  return (pcall(mem.write_u8, addr, value, domain))
end

-- ── Bit AND (pure Lua, no bit library dependency) ─────────────────────────────
local function bit_and(a, b)
  local res, bitval = 0, 1
  while a > 0 and b > 0 do
    if a % 2 == 1 and b % 2 == 1 then res = res + bitval end
    a = math.floor(a / 2); b = math.floor(b / 2); bitval = bitval * 2
  end
  return res
end

-- ── String read from a memory domain (client drops 0 bytes) ───────────────────
local function read_string(addr, len, domain)
  local out = {}
  for i = 0, len - 1 do
    local b = read_u8(addr + i, domain)
    if b == nil then return nil end
    if b ~= 0 then out[#out + 1] = string.char(b) end
  end
  return table.concat(out)
end

-- ── ROM identity: validate_rom checks "GBAZELDA" @0xA0; the AP patch additionally
--    writes the player name @0x600 (zero on a vanilla cartridge). Gating on BOTH
--    means the module never acts on a stock dump or a different GBA Zelda. ──────
local function rom_is_ap()
  if rom_ok ~= nil then return rom_ok end
  local name = read_string(ROM_NAME_ADDR, #ROM_NAME, ROM)
  if name == nil then return false end           -- not readable yet; retry next poll
  if name ~= ROM_NAME then
    rom_ok = false
    log("non-Minish-Cap ROM (id '" .. tostring(name) .. "') — detection idle")
    return false
  end
  -- AP-patch marker: the player name at ROM 0x600 is non-empty only on a patched ROM.
  local marker = read_string(AP_MARKER_ADDR, AP_MARKER_LEN, ROM)
  if marker == nil then return false end
  if #marker == 0 then
    rom_ok = false
    log("Minish Cap ROM has no AP player-name marker @0x600 — vanilla cartridge, detection idle")
    return false
  end
  rom_ok = true
  log("AP Minish Cap ROM verified (id 'GBAZELDA', slot '" .. marker .. "')")
  return true
end

-- ── Multiworld context ────────────────────────────────────────────────────────
local function load_locations(ids)
  if type(ids) ~= "table" then return end
  server_locations = {}
  local n = 0
  for _, id in ipairs(ids) do
    local v = tonumber(id)
    if v then server_locations[v] = true; n = n + 1 end
  end
  log("server location set: " .. n .. " ids")
end

local function wanted(ap_id)
  if server_locations == nil then return true end
  return server_locations[ap_id] == true
end

-- ── In-game gate (game_watcher line ~240) ─────────────────────────────────────
-- The client processes locations/items only when game_task == 0x02 OR
-- task_substate == 0x02 (i.e. normal gameplay update). File/title screens never
-- satisfy this, so no check is ever reported off the title-screen demo.
local function in_gameplay(game_task, task_substate)
  return game_task == GAME_TASK_GAMEPLAY or task_substate == GAME_TASK_GAMEPLAY
end

-- ── Item delivery (handle_item_receiving, client.py line ~257) ────────────────
-- One guarded write per poll: write the 2-byte AP id into the mailbox @0x3FF10
-- only while it reads [0,0] AND the "player safe" byte @0x2A4A == 1, then advance
-- the received-item counter (u16 BE @0x2A44). The game's ASM consumes the mailbox
-- (zeroes it) — exactly the client's guarded_write + counter-bump handshake.
-- Caller has already verified rom_ok + in-gameplay, so this never writes into a
-- foreign / unpatched cartridge.
local function deliver_next()
  -- Mailbox must be empty (game has consumed the previous item).
  local mb0 = read_u8(ITEM_MAILBOX_ADDR,     EWRAM)
  local mb1 = read_u8(ITEM_MAILBOX_ADDR + 1, EWRAM)
  if mb0 == nil or mb1 == nil or mb0 ~= 0 or mb1 ~= 0 then return end
  -- "Player safe" guard: only deliver when the ROM says it is safe to receive.
  local safe = read_u8(PLAYER_SAFE_ADDR, EWRAM)
  if safe ~= 1 then return end

  local hi = read_u8(RECV_INDEX_ADDR,     EWRAM)
  local lo = read_u8(RECV_INDEX_ADDR + 1, EWRAM)
  if hi == nil or lo == nil then return end
  local received_index = (hi * 0x100) + lo

  local item = raw_items[received_index]            -- 0-based stream index
  if item == nil then return end                    -- nothing new to deliver

  local id  = item.id
  local pid = math.floor(id / 0x100) % 0x100        -- id >> 8
  local sid = id % 0x100                            -- id & 0xFF

  -- Guarded write: mailbox high+low bytes. (Re-checked [0,0] above this frame.)
  if not write_u8(ITEM_MAILBOX_ADDR,     pid, EWRAM) then return end
  if not write_u8(ITEM_MAILBOX_ADDR + 1, sid, EWRAM) then return end

  -- Advance the received-item counter (u16 BE).
  local nxt = received_index + 1
  write_u8(RECV_INDEX_ADDR,     math.floor(nxt / 0x100) % 0x100, EWRAM)
  write_u8(RECV_INDEX_ADDR + 1, nxt % 0x100, EWRAM)

  delivered_log = delivered_log + 1
  if delivered_log <= 5 or delivered_log % 25 == 0 then
    log(string.format("delivered item #%d (AP id %d -> pid 0x%X sid 0x%X) as index %d",
                      delivered_log, id, pid, sid, nxt))
  end
end

-- ── Module contract ───────────────────────────────────────────────────────────
function M.init(ctx)
  if ctx and type(ctx.log) == "function" then log_fn = ctx.log end
  if not resolve_memory_api() then
    log("BizHawk memory API unavailable — module idle")
    ADDRESSES_VERIFIED = false
    return
  end
  local cfg = (ctx and ctx.config) or {}
  load_locations(cfg.locations)
  local n = 0; for _ in pairs(LOC) do n = n + 1 end
  log("ready: " .. n .. " location flags, item delivery active")
end

function M.poll()
  local new = {}
  if not ADDRESSES_VERIFIED then return new end
  if not rom_is_ap() then return new end

  local game_task     = read_u8(GAME_TASK_ADDR,     IWRAM)
  local task_substate = read_u8(TASK_SUBSTATE_ADDR, IWRAM)
  if game_task == nil or task_substate == nil then return new end

  -- Goal: Vaati's defeat sets game_task to 0x04 (checked before the gameplay
  -- gate, because the same byte being 0x04 means we are no longer in 0x02).
  if not goal_reached and game_task == GAME_TASK_GOAL then
    goal_reached = true
    log("goal reached (game_task == 0x04 — Vaati defeated)")
  end

  if not in_gameplay(game_task, task_substate) then return new end

  -- Location detection: (EWRAM[addr] | mask) == EWRAM[addr] for each wanted loc.
  for ap_id, def in pairs(LOC) do
    if not reported[ap_id] and wanted(ap_id) then
      local byte = read_u8(def[1], EWRAM)
      if byte ~= nil and bit_and(byte, def[2]) == def[2] then
        reported[ap_id] = true
        new[#new + 1] = ap_id
      end
    end
  end

  -- Item delivery shares this poll's verified gate (rom_ok + in-gameplay).
  deliver_next()

  return new
end

function M.is_goal_complete()
  return goal_reached
end

-- Enqueue a received AP item by its ABSOLUTE stream index. Delivery happens in
-- poll() via the game's own received-count handshake (EWRAM 0x2A44 / 0x3FF10).
function M.receive_item(item_id, meta)
  local id = tonumber(item_id)
  if not id then return end

  if meta and meta.index ~= nil then
    local idx = tonumber(meta.index)
    if idx and idx >= 0 then
      raw_items[idx] = {
        id       = id,
        player   = tonumber(meta.player),
        flags    = tonumber(meta.flags),
        location = tonumber(meta.location),
      }
      if idx >= legacy_next then legacy_next = idx + 1 end
      return
    end
  end

  -- Legacy plain "ITEM:<id>" line — append in arrival order.
  raw_items[legacy_next] = { id = id }
  legacy_next = legacy_next + 1
end

return M
