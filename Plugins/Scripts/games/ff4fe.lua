-- ff4fe.lua — game module for the Archipelago BizHawk connector.
--             Final Fantasy IV: Free Enterprise (SNES)
--
-- STATUS: location DETECTION + goal are REAL and SOURCE-DERIVED from the official
-- AP world worlds/ff4fe (Client.py + locations.py + rom.py), the open PR adding the
-- game (ArchipelagoMW/Archipelago#3897, head Rosalie-A/Archipelago branch
-- Final-Fantasy-4-Free-Enterprise). The location tables (399 treasure + 75 reward +
-- 32 objective = 506 of the world's 508 locations) were GENERATED by PARSING the
-- source — re-running the exact locations.py `all_locations` construction over the
-- world's own data/treasure.csvdb + data/rewardslots.csvdb through its own csvdb.py,
-- then assigning AP ids the way FF4FEWorld does (base_id 7191991 + position in
-- all_locations) and splitting them by the in-game decode category FF4FEClient uses
-- — not hand-copied, so they are exact (mock-verified through MoonSharp against
-- synthetic SNES memory). Loads crash-free on any ROM; self-disables on a
-- non-AP/non-FF4FE cartridge.
--
-- MEMORY MODEL (SNI → BizHawk)  — same translation the A Link to the Past and Super
-- Metroid modules document (FF4FE's client is an SNIClient too):
-- ────────────────────────────
--   SNI WRAM_START(0xF50000)+off → domain "WRAM",    offset off (= $7E0000+off)
--   SNI SRAM_START(0xE00000)+off → domain "CARTRAM", offset off (fallback "SRAM")
--   The ROM-name the client validates is read at SNI 0x007FC0 (the LoROM internal
--   header) — in BizHawk the cartridge ROM ("CARTROM"/"ROM") at file offset 0x7FC0,
--   probed multi-domain with fallbacks like the EarthBound module's "MOM2AP" probe.
--   The AP build writes a custom header beginning "4FE" there (rom.py
--   FF4FEPatchExtension.call_fe + __init__ generate_output: rom_name = "4FE<ver>_…"),
--   exactly what Client.validate_rom checks (rom_name[:3] == b"4FE").
--
-- WHAT THIS DOES (mirrors worlds/ff4fe/Client.py game_watcher)
--   • poll(): the client's three flag scans → AP location ids, gated to the slot's
--     server location set and to the SAFE-STATE sentinel gate (Client.connection_check
--     reads six "are we in a cutscene/battle/menu" sentinel bytes; ALL must be 0 or it
--     does nothing — we mirror that so the menu's zeroed flag arrays never report
--     phantom checks).
--       - Treasure: byte=bit//8, bit%8 in the 64-byte array at WRAM 0x12A0,
--         LSB-first (treasure_data[byte] & (2**bit)); in-game bit index == fe_id.
--       - Reward:   same decode in the 16-byte array at WRAM 0x1510; in-game bit i
--         maps to fe_id i+0x200. (The client force-flags reward bit 1 — FE never
--         flags your starting character — so we do too.)
--       - Objectives: the 32-byte progress array at WRAM 0x1520; objective i is done
--         when objective_progress[i] > 0. Reported only while the objective COUNT
--         (SNI cart 0x10F0FC) is readable and > 0; otherwise objectives are simply
--         not scanned (graceful — never a false report).
--   • is_goal_complete(): victory byte at SRAM 0x070F2 > 0, behind the same six
--     sentinels read < 0x02 (Client.check_victory exactly — the >= 0x02 guard defends
--     against RAM initialised to static values everywhere).
--   • receive_item(): NO-OP for now (documented). items_handling = 0b111 — the AP
--     SERVER drives item delivery, but FF4FE's apply path is NOT a simple buffered
--     write: the reference client parses the live inventory, junk-tier/sell settings
--     and the seed's flag string, auto-sells junked items into the GP counter, sets
--     five special key-item flag bits, and threads items into free inventory slots
--     gated on the game's own received-item counter (SRAM 0x02FFE). That multi-read
--     decision logic is the documented deferred piece — a wrong WRAM/SRAM write would
--     corrupt the save — so it is intentionally left out rather than shipped
--     unverified, exactly like the other SNES modules in this wave. Checks + goal
--     flow regardless.
--
-- MODULE CONTRACT (called by bizhawk_ap_connector.lua)
--   M.init(ctx) / M.poll() -> {ids} / M.is_goal_complete() -> bool /
--   M.receive_item(item_id, meta)
-- ═══════════════════════════════════════════════════════════════════════════════

