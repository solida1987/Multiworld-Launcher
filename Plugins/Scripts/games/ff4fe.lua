-- ── Logging ───────────────────────────────────────────────────────────────────
local function log(msg)
  if log_fn then pcall(log_fn, "[ff4fe] " .. tostring(msg)) end
end

-- ── Memory API (resolved at init; 2-arg domain form + current-domain fallback) ─
local function resolve_memory_api()
  if not memory then return false end
  mem.read_u8 = memory.read_u8 or memory.readbyte
  return mem.read_u8 ~= nil
end

local function rd(addr, domain)
  if not mem.read_u8 then return nil end
  local ok, v = pcall(mem.read_u8, addr, domain)
  if ok and type(v) == "number" then return v end
  ok, v = pcall(mem.read_u8, addr)              -- older API: current domain
  if ok and type(v) == "number" then return v end
  return nil
end

local function read_u8(addr, domain) return rd(addr, domain or WRAM) end

-- Read from CARTRAM, falling back to the "SRAM" domain name on cores that use it.
local function read_cartram_u8(off)
  local ok, v = pcall(mem.read_u8, off, CARTRAM)
  if ok and type(v) == "number" then return v end
  ok, v = pcall(mem.read_u8, off, "SRAM")
  if ok and type(v) == "number" then return v end
  return nil
end

-- Read one byte trying a list of {domain, addr} pairs in order (for the ROM sig).
local function read_first(pairs_list)
  for _, p in ipairs(pairs_list) do
    local ok, v = pcall(mem.read_u8, p[2], p[1])
    if ok and type(v) == "number" then return v, p end
  end
  return nil
end

local function bit_and(a, b)
  -- 16-bit safe AND without the bit library (cores vary).
  local res, bitval = 0, 1
  while a > 0 and b > 0 do
    if a % 2 == 1 and b % 2 == 1 then res = res + bitval end
    a = math.floor(a / 2); b = math.floor(b / 2); bitval = bitval * 2
  end
  return res
end

-- LSB-first single-bit test inside a byte array (treasure_data[byte] & (2**bit)).
local POW2 = { [0]=1, 2, 4, 8, 16, 32, 64, 128 }
local function bit_set_lsb(byte_array, bit_index)
  local b = byte_array[math.floor(bit_index / 8)]
  if b == nil then return false end
  return bit_and(b, POW2[bit_index % 8]) ~= 0
end

-- Domain read for a {domain, off} sentinel pair.
local function read_sentinel(s)
  if s[1] == CARTRAM then return read_cartram_u8(s[2]) end
  return read_u8(s[2], s[1])
end

-- ── ROM identity: the FF4FE AP build's header begins "4FE" ─────────────────────
-- Tries CARTROM/ROM LoROM 0x7FC0, the System Bus header, then the HiROM offset.
-- Whichever domain yields "4FE" is cached and reused.
local function rom_is_ap()
  if rom_ok ~= nil then return rom_ok end
  local first, picked = read_first({
    { "CARTROM",    SIG_CARTROM_LO },
    { "ROM",        SIG_CARTROM_LO },
    { "System Bus", SIG_BUS_ADDR },
    { "CARTROM",    SIG_CARTROM_HI },
    { "ROM",        SIG_CARTROM_HI },
  })
  if first == nil then return false end           -- ROM not readable yet; retry
  if first ~= string.byte(ROM_SIG, 1) then
    rom_ok = false
    log("non-FF4FE/non-AP ROM (no '4FE' header) — detection idle, no writes")
    return false
  end
  for i = 2, #ROM_SIG do
    local ok, b = pcall(mem.read_u8, picked[2] + i - 1, picked[1])
    if not ok or type(b) ~= "number" then return false end
    if b ~= string.byte(ROM_SIG, i) then
      rom_ok = false
      log("non-FF4FE/non-AP ROM (no '4FE' header) — detection idle, no writes")
      return false
    end
  end
  rom_ok = true
  sig_domain = picked
  log(("FF4FE ROM verified ('4FE' header via %s 0x%X)"):format(picked[1], picked[2]))
  return true
end

-- ── Multiworld context ────────────────────────────────────────────────────────
local function load_locations(ids)
  if type(ids) ~= "table" then return end
  server_locations = {}
  local n = 0
  for _, id in ipairs(ids) do
    local v = tonumber(id)
    if v then server_locations[v] = true; n = n + 1 end
  end
  log("server location set: " .. n .. " ids")
end

local function wanted(ap_id)
  if server_locations == nil then return true end   -- no set provided → report all
  return server_locations[ap_id] == true
end

-- Item-stream filter, same policy as the ALttP/FFMQ modules. Even though FF4FE is
-- items_handling=0b111 (server-driven), the in-game write path is deferred, so this
-- only feeds a future delivery wiring; own-location items the game already granted
-- are dropped and server/starting entries dropped.
local function accept_item(it)
  if it.player == nil then return true end          -- legacy ITEM:<id> line
  if it.player == 0 and (it.location or 0) <= 0 then return false end
  return true
end

-- ── Snapshot + safe-state gate ────────────────────────────────────────────────
local function refresh_snapshot()
  for i = 0, TREASURE_BYTES - 1 do treasure[i] = read_u8(TREASURE_OFF + i, WRAM) end
  for i = 0, REWARD_BYTES   - 1 do reward[i]   = read_u8(REWARD_OFF   + i, WRAM) end
  for i = 0, OBJPROG_BYTES  - 1 do objprog[i]  = read_u8(OBJPROG_OFF  + i, WRAM) end
end

-- connection_check gate: every sentinel must read exactly 0 (in a safe gameplay
-- state, not a cutscene/battle/menu, and not RAM still initialised to junk).
local function in_safe_state()
  for _, s in ipairs(SENTINELS) do
    local v = read_sentinel(s)
    if v == nil or v ~= 0 then return false end
  end
  return true
end

-- Objective count from SNI cart space (bank 0x10). LoROM file offset for
-- 0x10F0FC = ((0x10 & 0x7F) << 15) | (0xF0FC & 0x7FFF) = 0x087EFC. Probed in the
-- ROM domains; nil when unreadable (objectives then simply aren't scanned).
local function lorom_file_offset(sni_addr)
  local bank = math.floor(sni_addr / 0x10000)
  local addr = sni_addr % 0x10000
  return ((bank % 0x80) * 0x8000) + (addr % 0x8000)
end

local function read_cart_rom_u8(sni_addr)
  local off = lorom_file_offset(sni_addr)
  local v, _ = read_first({
    { "CARTROM",    off },
    { "ROM",        off },
    { "System Bus", sni_addr % 0x1000000 },
  })
  return v
end

local function objective_count()
  return read_cart_rom_u8(OBJ_COUNT_CARTADDR)
end

local function scan_into(new)
  -- Treasure: LSB-first bit == fe_id in the 64-byte array.
  for ap_id, bit in pairs(LOC_TREASURE) do
    if not reported[ap_id] and wanted(ap_id) and bit_set_lsb(treasure, bit) then
      reported[ap_id] = true; new[#new + 1] = ap_id
    end
  end
  -- Reward: LSB-first bit in the 16-byte array (bit 1 force-flagged like the client).
  for ap_id, bit in pairs(LOC_REWARD) do
    if not reported[ap_id] and wanted(ap_id) then
      if bit == REWARD_FORCE_BIT or bit_set_lsb(reward, bit) then
        reported[ap_id] = true; new[#new + 1] = ap_id
      end
    end
  end
  -- Objectives: progress[index] > 0, but only when the seed actually has objectives.
  local oc = objective_count()
  if oc ~= nil and oc > 0 then
    for ap_id, idx in pairs(LOC_OBJECTIVE) do
      if not reported[ap_id] and wanted(ap_id) and idx < oc then
        local v = objprog[idx]
        if v ~= nil and v > 0 then reported[ap_id] = true; new[#new + 1] = ap_id end
      end
    end
  end
end

-- ── Module contract ───────────────────────────────────────────────────────────

function M.init(ctx)
  if ctx and type(ctx.log) == "function" then log_fn = ctx.log end
  if not resolve_memory_api() then
    log("BizHawk memory API unavailable — module idle")
    ADDRESSES_VERIFIED = false
    return
  end
  local cfg = (ctx and ctx.config) or {}
  slot_number = tonumber(cfg.slot_number) or 0
  load_locations(cfg.locations)
  local n = 0
  for _ in pairs(LOC_TREASURE)  do n = n + 1 end
  for _ in pairs(LOC_REWARD)    do n = n + 1 end
  for _ in pairs(LOC_OBJECTIVE) do n = n + 1 end
  log(("ready: slot #%d, %d location flags"):format(slot_number, n))
end

function M.poll()
  local new = {}
  if not ADDRESSES_VERIFIED then return new end
  if not rom_is_ap() then return new end
  if not in_safe_state() then return new end
  refresh_snapshot()
  scan_into(new)
  return new
end

function M.is_goal_complete()
  if not ADDRESSES_VERIFIED or not rom_is_ap() then return false end
  -- check_victory: every sentinel must read < 0x02, then victory byte > 0.
  for _, s in ipairs(SENTINELS) do
    local v = read_sentinel(s)
    if v == nil or v >= 0x02 then return false end
  end
  local victory = read_cartram_u8(VICTORY_OFF)
  return victory ~= nil and victory > 0
end

-- Remote items: see the file header. items_handling = 0b111 (the AP server drives
-- delivery), but FF4FE's apply path is intricate decision logic over the live
-- inventory / junk-tier / sell settings / seed flags, with guarded WRAM+SRAM writes
-- gated on the game's own received-item counter — the documented deferred piece. We
-- record the stream (so a future wiring has it) but make no write (never a wrong
-- WRAM/SRAM write) until it can be confirmed in-emulator. Checks + goal flow
-- regardless.
function M.receive_item(item_id, meta)
  if type(meta) == "table" then
    items_received[#items_received + 1] = {
      item     = tonumber(item_id),
      index    = tonumber(meta.index),
      player   = tonumber(meta.player),
      flags    = tonumber(meta.flags),
      location = tonumber(meta.locId),
    }
  else
    items_received[#items_received + 1] = { item = tonumber(item_id) }
  end
  -- intentionally no memory write (documented; deferred until in-emulator verified)
end

return M
