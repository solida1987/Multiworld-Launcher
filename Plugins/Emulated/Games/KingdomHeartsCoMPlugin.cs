using System;
using System.Collections.Generic;
using System.IO;
using System.Text.Json;
using System.Threading;
using System.Threading.Tasks;
using LauncherV2.Core;

namespace LauncherV2.Plugins.Emulated.Games;

// ═══════════════════════════════════════════════════════════════════════════════
// KingdomHeartsCoMPlugin — Archipelago integration for Kingdom Hearts: Chain of
// Memories (GBA), on the proven BizHawk Lua-pipe bridge.
//
// COMMUNITY APWORLD. KHCOM is NOT in ArchipelagoMW/Archipelago main; it is
// gaithern's custom world:
//   https://github.com/gaithernOrg/ArchipelagoKHCOM   (game string
//   "Kingdom Hearts Chain of Memories", required_client_version 0.3.5).
//
// STATUS: location DETECTION + goal are REAL and SOURCE-DERIVED. The 153-location
// table in khcom.lua (152 "journal" completion bits + Final Marluxia) was
// GENERATED by parsing the world's own BizHawk client
// (data/lua/connector_khcom.lua define_journal_bit_location_ids()) with Python and
// cross-checked 1:1 against worlds/khcom/Locations.py (0 gaps); the journal
// (byte,bit) math and the Final-Marluxia position/floor goal test are replicated
// exactly from connector_khcom.lua (check_journal / check_final_marluxia), and
// mock-verified through MoonSharp against synthetic GBA EWRAM.
//
// items_handling = 0b111 (FULL REMOTE): the AP SERVER drives ALL item delivery.
// The reference client applies received items through an intricate LIVE path —
// decode the item name, then inject specific battle-card values into the card
// array at EWRAM 0x0203A080, rewrite deck pointers, open randomized card packs,
// and grant gold map cards / world unlocks, all with per-frame deck bookkeeping.
// That guarded multi-write path depends on state only confirmable in-emulator, so
// it is deferred (khcom.lua receive_item is a documented no-op) rather than
// shipped unverified — a wrong write would corrupt the deck/save. Checks + goal
// flow regardless; a solo seed reports every location.
//
// HOW KHCOM IS RANDOMIZED — IMPORTANT: like Final Fantasy 1, KHCOM is NOT patched
// by Archipelago. There is NO APProcedurePatch container (.apkhcom), no Rom.py, no
// generate_output, and no base_checksum — the world emits only multiworld logic.
// The player loads a VANILLA Kingdom Hearts: Chain of Memories (US) cartridge dump
// directly in BizHawk; ALL randomization (starting deck, removing picked-up cards,
// injecting items, gating floors) happens live in connector_khcom.lua from work
// RAM. So this plugin applies no patch of its own: PrepareSessionRomAsync returns
// null and the base class launches the library ROM as-is (§11: the launcher copy,
// the original is never modified). The ROM is identified by CONTENT (size + MD5),
// never by filename (§11).
// ═══════════════════════════════════════════════════════════════════════════════

public sealed class KingdomHeartsCoMPlugin : EmulatorPlugin
{
    public KingdomHeartsCoMPlugin() => LoadSettings();

    // ── Identity ──────────────────────────────────────────────────────────────

    public override string GameId      => "khcom";
    public override string DisplayName => "Kingdom Hearts: Chain of Memories";
    public override string Subtitle    => "GBA · Emulated";

    // Exact AP world id ("game" string in KHCOMWorld / KHCOMContext.game).
    public override string ApWorldName => "Kingdom Hearts Chain of Memories";

    public override string Description =>
        "Kingdom Hearts: Chain of Memories is the GBA card-battling action RPG that " +
        "follows Sora through the floors of Castle Oblivion. In the Archipelago " +
        "randomizer, attack/magic/item/enemy cards, gold map cards, world unlocks and " +
        "friend cards across all thirteen floors join the multiworld pool. Defeat the " +
        "final Marluxia to complete your goal. You supply your own vanilla US ROM — the " +
        "randomizer runs entirely from the game's memory, no patch required.";

    public override string ThemeAccentColor => "#3A6FB0";   // Kingdom Hearts ocean blue

    // ── Emulator specifics ────────────────────────────────────────────────────

    protected override string RomSystem     => "GBA";

    // §14: GBA emulators — BizHawk verified; mGBA/Mesen are the alternatives
    // (shown per their bridge state in the dropdown).
    protected override IReadOnlyList<string> SupportedEmulatorIds =>
        new[] { "bizhawk", "mgba", "mesen" };
    protected override string LuaScriptName => "bizhawk_ap_connector.lua";
    protected override string LuaModuleName => "khcom";

    /// khcom.lua reports checks + goal (journal-bit table generated from the
    /// apworld; Final-Marluxia goal test from connector_khcom.lua; mock-verified).
    /// Remote-item delivery is the documented deferred piece (items_handling =
    /// 0b111, intricate live card-array injection).
    public override bool ChecksImplemented => true;

    /// The vanilla cartridge dump the randomizer runs against, by CONTENT
    /// (8 MB + MD5) — never by filename (§11). KHCOM emits no patch, so there is
    /// no base_checksum to validate against; the canonical No-Intro US dump is
    /// pinned here, and (like FF1's vanilla entry) any other 8 MB GBA file is also
    /// accepted so a player is not blocked at import — the connector verifies the
    /// game live and only the real KHCOM (US) ROM produces checks.
    protected override IReadOnlyList<RomIdentity> AcceptableBaseRoms => new[]
    {
        new RomIdentity(8 * 1024 * 1024, "f7b81e3f3bb3b02d973fc6f145ad4416",
                        "Kingdom Hearts: Chain of Memories (USA)"),
        new RomIdentity(8 * 1024 * 1024, null,
                        "Kingdom Hearts: Chain of Memories (US) — GBA cartridge dump"),
    };

    // ── AP patch (KHCOM emits none — optional override kept for parity) ───────────
    //
    // KHCOM has no APProcedurePatch (see header). We keep the standard hook shape
    // for consistency with the other emulated games, but it is inert: if a player
    // ever drops an explicit patch container in Settings it would be honored,
    // otherwise the vanilla ROM is loaded directly with no patch step. The AP
    // generator never emits one.

    private const string PatchExt = ".apkhcom";

    /// Explicit patch override chosen by the user (Settings / drag-drop). Null =
    /// none — the normal KHCOM path, the vanilla ROM is loaded as-is.
    public string? ApPatchPath { get; private set; }

    protected override void OnSavingSettings(IDictionary<string, object?> bag)
        => bag["ap_patch_path"] = ApPatchPath;

    protected override void OnLoadingSettings(JsonElement root)
    {
        if (root.TryGetProperty("ap_patch_path", out var el)) ApPatchPath = el.GetString();
    }

    /// Set an explicit patch override (rare for KHCOM) and persist it.
    public void SetExplicitPatch(string path) { ApPatchPath = path; SaveSettings(); }

    /// ROM safety net. KHCOM has no patch carrying an expected MD5, so the only
    /// requirement we can surface is "a KHCOM ROM is present" — the player supplies
    /// the vanilla US dump themselves. A future explicit .apkhcom override (which
    /// the AP generator does not emit) would still demand its base MD5 if one is set.
    public override RomRequirement? GetUnmetRomRequirement()
    {
        var patch = SnesApPatchHelper.ResolvePatch(
            PatchExt, ApPatchPath, GetSeedName?.Invoke(), CurrentSlotName, out _);
        string? wantMd5 = patch != null
            ? SnesApPatchHelper.ReadManifestField(patch, "base_checksum") : null;
        bool haveRom = RomPath != null && File.Exists(RomPath);

        if (wantMd5 == null)
            return haveRom ? null
                 : new RomRequirement("Kingdom Hearts: Chain of Memories", "GBA",
                       "Kingdom Hearts: Chain of Memories (USA) — your own cartridge dump",
                       RequiredMd5: null, WrongVersionPresent: false, BuildRomFilter());

        if (haveRom && ComputeMd5(RomPath!).Equals(wantMd5, StringComparison.OrdinalIgnoreCase))
            return null;

        return new RomRequirement("Kingdom Hearts: Chain of Memories", "GBA",
            "Kingdom Hearts: Chain of Memories (USA) — the exact ROM this patch was generated against",
            wantMd5, WrongVersionPresent: haveRom, BuildRomFilter());
    }

    // ── Session ROM ───────────────────────────────────────────────────────────
    //
    // The normal KHCOM path applies NO patch — the player's library ROM is the
    // vanilla US cartridge, so we return null and the base class launches it
    // directly. Only an explicit .apkhcom override (not emitted by the AP
    // generator) would trigger SnesApPatchHelper; that branch is kept for parity
    // with the other emulated games but is inert for standard KHCOM seeds.

    protected override async Task<string?> PrepareSessionRomAsync(
        ApSession session, CancellationToken ct)
    {
        string? patch = SnesApPatchHelper.ResolvePatch(
            PatchExt, ApPatchPath, GetSeedName?.Invoke(), CurrentSlotName, out string how);
        if (patch == null)
        {
            // Standard KHCOM: no AP patch container exists. The library ROM is the
            // vanilla US cartridge; the connector randomizes it live from RAM.
            SessionRomNote =
                "[Kingdom Hearts: Chain of Memories] KHCOM is randomized live by its " +
                "BizHawk connector (not by an Archipelago patch) — launching your ROM " +
                "directly. Make sure this is the vanilla Kingdom Hearts: Chain of " +
                "Memories (US) cartridge dump.";
            return null;
        }

        // Optional explicit-override path (kept for parity; KHCOM never emits .apkhcom).
        var res = await SnesApPatchHelper.ApplyAsync(
            "Kingdom Hearts: Chain of Memories", patch, how, RomPath!,
            RomLibraryDirectory, ".gba", ct);
        SessionRomNote = res.Note;
        if (res.OutRom != null) RegisterSeed(res.OutRom, patch);
        return res.OutRom;
    }

    /// Record a produced/reused patched ROM in the seed library. Never throws.
    /// (Only reached on the optional explicit-override path — standard KHCOM seeds
    /// launch the vanilla ROM directly and never get here.)
    private void RegisterSeed(string outRom, string patch)
    {
        try
        {
            SeedLibraryStore.Instance.Register(new SeedEntry
            {
                GameId         = GameId,
                PatchedRomPath = outRom,
                PatchPath      = patch,
                SeedName       = SnesApPatchHelper.ParseSeedFromPatch(patch),
                SlotName       = CurrentSlotName ?? "",
                CreatedUtc     = DateTimeOffset.UtcNow,
            });
        }
        catch { /* registry is non-essential to launching */ }
    }
}
