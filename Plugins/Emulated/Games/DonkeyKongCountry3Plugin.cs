// game string "Donkey Kong Country 3", patch_suffix ".apdkc3", items_handling
// 0b111 (FULL remote). The patch's declared base ROM is the US 1.0 dump (MD5
// 120abf304f0c40fe059f6a192ed4f947 = USHASH in worlds/dkc3/Rom.py).
